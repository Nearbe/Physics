#!/usr/bin/env python3
"""
КАЛИБРОВОЧНОЕ ПОЛЕ [-1, +1]
═══════════════════════════════════════════════════════════════════════

ОГРОМНОЕ КАЛИБРОВОЧНОЕ ПОЛЕ ДЛЯ ФОРМУЛ

Теперь у нас есть универсальная система координат:
  → Любую формулу можно откалибровать в это поле
  → Все параметры в одном диапазоне [-1, +1]
  → Видим связи между разными вещами

ЧТО ТАКОЕ КАЛИБРОВОЧНОЕ ПОЛЕ:
────────────────────────────
Калибровочное поле = пространство, где всё нормировано
Как "конвейер" — всё проходит через одну систему координат

В нашем случае: каждое число → нормализация → [-1, +1]

КАЛИБРОВКА ФОРМУЛ:
──────────────────

До калибровки:                          После калибровки:
  частота = 4.5e43 Hz                      частота = +0.9 (высокая)
  масса = 94.4 кг                          масса = +0.3 (средняя)
  Δ = 1.55                                 Δ = +0.55 (выше среднего)
  эмоция = +55 кг                          эмоция = +0.55 (позитивная)
  GPS = 58.5°N                            GPS = +0.65 (северное)

ПРЕИМУЩЕСТВА:
─────────────
1. Все параметры в одном диапазоне
2. Видим "вектор состояния" человека
3. Сравниваем любые вещи
4. Формулы становятся "прозрачными"
5. Калибровка = универсальный язык

ПРИМЕР: ФОРМУЛЫ ДО и ПОСЛЕ КАЛИБРОВКИ
──────────────────────────────────────

Формула 1: Δ → частота
  До:  f = Planck × Δ² = 4.5e43 Hz
  После: f_norm = (f - 0) / (1e44 - 0) × 2 - 1 = +0.9

Формула 2: Δ → масса
  До:  m = Planck × Δ² × 0.138 = 3e-9 kg
  После: m_norm = (m - 0) / (1e-8 - 0) × 2 - 1 = +0.7

Формула 3: вес → эмоция
  До:  эмоция = 55 кг (любовь)
  После: эмоция_norm = 55 / 100 = +0.55
"""

import math


# ============================================================================
# КАЛИБРОВОЧНОЕ ПОЛЕ: ВСЁ В [-1, +1]
# ============================================================================


class CalibrationField:
    """
    Универсальное калибровочное поле — все формулы нормируются в [-1, +1]
    """

    # Базовые константы для калибровки
    CALIBRATION_RANGES = {
        # Энергетические
        "частота": (0, 1e50),  # Hz
        "энергия": (0, 1e30),  # J
        "мощность": (0, 1e20),  # W
        # Массовые
        "масса": (0, 1e3),  # kg
        "эмоция_масса": (-100, 100),  # kg
        # Пространственные
        "длина": (0, 1e30),  # m
        "объём": (0, 1e90),  # m³
        "площадь": (0, 1e60),  # m²
        # Временные
        "время": (0, 1e20),  # s
        "возраст": (0, 150),  # лет
        # Информационные
        "биты": (0, 1e30),
        "IQ": (50, 150),
        # Географические
        "широта": (-90, 90),
        "долгота": (-180, 180),
        "высота": (-1000, 9000),
        # Социальные
        "доход": (0, 1e7),  # USD
        "популярность": (0, 1e12),  # подписчики
        # Векторные
        "delta": (0, 2),  # Δ
        "заряд": (1, 10),  # Q = e^Δ
        "размерность": (2, 4),  # D_f
    }

    @staticmethod
    def calibrate(value: float, param_name: str) -> float:
        """Откалибровать значение в диапазоне [-1, +1]"""
        if param_name not in CalibrationField.CALIBRATION_RANGES:
            return 0.0

        min_val, max_val = CalibrationField.CALIBRATION_RANGES[param_name]

        if max_val == min_val:
            return 0.0

        normalized = (value - min_val) / (max_val - min_val) * 2 - 1
        return max(-1.0, min(1.0, normalized))

    @staticmethod
    def calibrate_formula(name: str, formula_func, param_values: dict) -> dict:
        """Откалибровать результат формулы"""
        raw_result = formula_func(**param_values)
        calibrated = CalibrationField.calibrate(raw_result, name)

        return {
            "формула": name,
            "входные_параметры": param_values,
            "сырой_результат": raw_result,
            "откалиброванный": calibrated,
            "вектор": calibrated,
        }


# ============================================================================
# ПРИМЕРЫ КАЛИБРОВКИ ФОРМУЛ
# ============================================================================


def calibrate_all_formulas():
    """Откалибровать все формулы системы"""

    print("=" * 80)
    print("ОГРОМНОЕ КАЛИБРОВОЧНОЕ ПОЛЕ")
    print("=" * 80)

    # Δ-система
    print("\n--- Δ-СИСТЕМА ---")

    delta_values = [0, 0.5, 1.0, 1.5, 2.0]

    for d in delta_values:
        freq = 1.854e43 * (d**2)
        mass = 2.176e-8 * (d**2) * 0.138
        charge = math.e**d
        dim = 2 + d

        # Калибровка
        freq_c = CalibrationField.calibrate(freq, "частота")
        mass_c = CalibrationField.calibrate(mass, "масса")
        charge_c = CalibrationField.calibrate(charge, "заряд")
        dim_c = CalibrationField.calibrate(dim, "размерность")

        print(f"\nΔ = {d}:")
        print(f"  частота:     {freq:>12.2e} → {freq_c:>+7.3f}")
        print(f"  масса:       {mass:>12.2e} → {mass_c:>+7.3f}")
        print(f"  заряд Q:     {charge:>12.4f} → {charge_c:>+7.3f}")
        print(f"  размерность: {dim:>12.1f} → {dim_c:>+7.3f}")

    # Человек
    print("\n--- ЧЕЛОВЕК (Псков, 94.4kg, любовь) ---")

    user_params = {
        "вес": 94.4,
        "рост": 175,
        "широта": 58.53313,
        "долгота": 31.21913,
        "эмоция": +55,  # любовь
        "возраст": 30,
    }

    for param, value in user_params.items():
        calibrated = CalibrationField.calibrate(value, param)

        # Визуализация
        bar = "─" * int((calibrated + 1) * 20)
        if calibrated > 0:
            bar = bar + "►"
        elif calibrated < 0:
            bar = "◄" + bar
        else:
            bar = "─" * 20 + "┼" + "─" * 20

        print(f"  {param:<10} {value:>10} → {calibrated:>+7.3f}  {bar}")


