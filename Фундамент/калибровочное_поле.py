def compare_people():
    """Сравнение людей через калибровочное поле"""

    print("\n" + "=" * 80)
    print("СРАВНЕНИЕ ЛЮДЕЙ")
    print("=" * 80)

    people = [
        {"имя": "Пользователь", "вес": 94.4, "рост": 175, "широта": 58.53, "эмоция": +55},
        {"имя": "Человек А", "вес": 70, "рост": 180, "широта": 40.71, "эмоция": +30},
        {"имя": "Человек Б", "вес": 120, "рост": 160, "широта": -33.87, "эмоция": -40},
    ]

    print("\nОткалиброванные векторы:")
    print("-" * 60)

    for person in people:
        vector = [
            CalibrationField.calibrate(person["вес"], "масса"),
            CalibrationField.calibrate(person["рост"], "рост"),
            CalibrationField.calibrate(person["широта"], "широта"),
            CalibrationField.calibrate(person["эмоция"], "эмоция_масса"),
        ]

        # Визуализация
        bar = " ".join([f"{v:>+5.2f}" for v in vector])
        print(f"  {person['имя']:<12} [{bar}]")

    print("-" * 60)
    print("Теперь видно кто ближе к кому в калибровочном поле!")


