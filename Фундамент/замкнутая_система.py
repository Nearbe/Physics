class FrequencyFromRelations:
    """
    Все частоты вычисляются через отношения базовых 83 частиц
    """

    # Базовые частоты (Гц) — упрощённо
    BASE_FREQUENCIES = {
        "electron": 1.235589e20,
        "proton": 2.268e17,
        "neutron": 2.257e17,
        "muon": 1.835e17,
        "tau": 3.077e16,
        "W_boson": 1.534e16,
        "Z_boson": 1.318e16,
        "Higgs": 1.265e16,
    }

    @staticmethod
    def compute_frequency(particle1: str, ratio: float) -> float:
        """Частота через отношение → энергетический канал"""
        base = FrequencyFromRelations.BASE_FREQUENCIES.get(particle1, 1e15)
        return base * ratio

    @staticmethod
    def show_all_computed():
        """Показать: все частоты вычисляемы"""
        print("\n" + "=" * 80)
        print("ВСЕ ЧАСТОТЫ ВЫЧИСЛЯЕМЫ ЧЕРЕЗ ОТНОШЕНИЯ")
        print("=" * 80)

        # Пример: вычисляем частоты через отношения
        examples = [
            ("electron", "m_p_m_e", 1836.15, "протон через отношение"),
            ("electron", "alpha", 137.036, "через тонкую структуру"),
            ("proton", "m_p_m_e", 1 / 1836.15, "электрон через отношение"),
            ("W_boson", "m_p_m_e", 1836.15 * 80, "W через протон"),
        ]

        for base, ratio_name, ratio, desc in examples:
            freq = FrequencyFromRelations.compute_frequency(base, ratio)
            print(f"  {base} × {ratio_name} = {ratio:.2e}")
            print(f"    → {freq:.3e} Hz ({desc})")


