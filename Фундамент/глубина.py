def verify_math():
    """Проверка математики"""

    print("\n" + "=" * 80)
    print("ПРОВЕРКА МАТЕМАТИКИ")
    print("=" * 80)

    # Δ → частота
    print("\nΔ → частота:")
    for d in [0.1, 0.5, 1.0, 1.5, 2.0]:
        f = 1.854e43 * d**2
        print(f"   Δ={d:<4} → f={f:.2e} Hz")

    # Δ → заряд Q = e^Δ
    print("\nΔ → заряд Q = e^Δ:")
    for d in [0, 0.5, 1.0, 1.5, 2.0]:
        q = math.e**d
        print(f"   Δ={d:<4} → Q={q:.3f}")

    # Δ → размерность D = 2 + Δ
    print("\nΔ → размерность D = 2 + Δ:")
    for d in [0, 0.5, 1.0, 1.5, 2.0]:
        dim = 2 + d
        print(f"   Δ={d:<4} → D={dim}")

    # Δ → эмоция (масса)
    print("\nΔ → эмоция (масса в кг):")
    for d in [0.1, 0.5, 1.0, 1.5, 2.0]:
        mass = d * 100  # 1 жизнь = 100 кг
        print(f"   Δ={d:<4} → {mass:>6.1f} кг")


