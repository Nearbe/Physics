def calibrate_human():
    """Калибровка человека в поле [-1, +1]"""

    print("\n" + "=" * 80)
    print("ЧЕЛОВЕК: 32 макро + 16 микро")
    print("=" * 80)

    # Данные
    data = {
        "вес": 94.4,
        "рост": 175,
        "возраст": 32,
        "Δ": 2.12,
        "частота": 8.336e43,
        "заряд": math.e**2.12,
        "размерность": 2 + 2.12,
    }

    # Нормализация
    ranges = {
        "вес": (0, 200),
        "рост": (100, 220),
        "возраст": (0, 120),
        "Δ": (0, 2),
        "частота": (1e40, 1e44),
        "заряд": (1, 10),
    }

    print("\nНормализованные значения:")
    for param, value in data.items():
        lo, hi = ranges.get(param, (0, 1))
        if hi != lo:
            norm = (value - lo) / (hi - lo) * 2 - 1
            norm = max(-1.0, min(1.0, norm))
            print(f"   {param:<12}: {value:>12} → {norm:>+7.3f}")


