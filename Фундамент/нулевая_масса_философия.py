    # Массы
    m_0 = 1212.34  # базовый масштаб (кг)
    m_zero = ZeroMassPhilosophy.mass_from_delta(m_0, 0)  # Δ=0
    m_human = ZeroMassPhilosophy.mass_from_delta(m_0, delta)  # Δ=1

