def normalize_all_constants():
    """Нормировать все константы в [-1, +1]"""

    print("=" * 100)
    print("НОРМИРОВАННЫЕ КОНСТАНТЫ")
    print("=" * 100)

    print(f"\n{'Константа':<10} {'Значение':<25} {'Норма':>8} {'Смысл'}")
    print("-" * 100)

    for name, data in ExtendedConstants.ALL.items():
        value = data["значение"]
        lo, hi = data["диапазон"]

        norm = (value - lo) / (hi - lo) * 2 - 1
        norm = max(-1.0, min(1.0, norm))

        print(f"{name:<10} {value:<25.3e} {norm:>+8.3f}  {data['смысл_в_системе']}")


