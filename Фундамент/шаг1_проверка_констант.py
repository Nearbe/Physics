def verify_mathematical_relations():
    """Проверить математические связи между константами"""

    print("\n" + "=" * 100)
    print("ПРОВЕРКА МАТЕМАТИЧЕСКИХ СВЯЗЕЙ")
    print("=" * 100)

    c = 299792458
    h = 6.62607015e-34
    hbar = h / (2 * math.pi)
    e = 1.602176634e-19
    epsilon_0 = 8.8541878128e-12
    mu_0 = 1.25663706212e-6
    alpha = 7.2973525693e-3
    m_e = 9.1093837015e-31
    m_p = 1.67262192369e-27

    # Проверка 1: c = 1/sqrt(epsilon_0 * mu_0)
    calculated_c = 1 / math.sqrt(epsilon_0 * mu_0)
    error_c = abs(calculated_c - c) / c * 100

    print(f"\n1. c = 1/sqrt(epsilon_0 * mu_0):")
    print(f"   Расчётное: {calculated_c:.0f} м/с")
    print(f"   Табличное:  {c:.0f} м/с")
    print(f"   Погрешность: {error_c:.6f}%")

    # Проверка 2: alpha = e^2 / (4*pi*epsilon_0*hbar*c)
    calculated_alpha = e**2 / (4 * math.pi * epsilon_0 * hbar * c)
    error_alpha = abs(calculated_alpha - alpha) / alpha * 100

    print(f"\n2. alpha = e^2 / (4π·ε₀·ħ·c):")
    print(f"   Расчётное: {calculated_alpha:.6f}")
    print(f"   Табличное:  {alpha:.6f}")
    print(f"   Погрешность: {error_alpha:.6f}%")

    # Проверка 3: m_p / m_e
    calculated_ratio = m_p / m_e
    table_ratio = 1836.15267343
    error_ratio = abs(calculated_ratio - table_ratio) / table_ratio * 100

    print(f"\n3. m_p / m_e:")
    print(f"   Расчётное: {calculated_ratio:.4f}")
    print(f"   Табличное:  {table_ratio:.4f}")
    print(f"   Погрешность: {error_ratio:.6f}%")

    # Проверка 4: e = Q при Δ=1 (заряд электрона)
    print(f"\n4. Q = e^Δ при Δ=1:")
    q_at_1 = math.e**1
    print(f"   e^1 = {q_at_1:.6f}")
    print(f"   Это заряд при Δ=1")


