#!/usr/bin/env python3
"""
ОТРИЦАТЕЛЬНАЯ МАССА
══════════════════════════════════════════════════════════════════════

Вес в диапазоне: от -100 кг до +100 кг

ОТРИЦАТЕЛЬНАЯ МАССА = "негативные" эмоции
• Положительная масса = эмоции притягивают (любовь, радость)
• Отрицательная масса = эмоции отталкивают (ненависть, страх)

Диапазон:
══════════════════════════════════════════════════════════════════════

  -100 кг          -50 кг            0            +50 кг          +100 кг
     │               │               │               │               │
     ▼               ▼               ▼               ▼               ▼
  АНТИ-ЭМОЦИИ    ОТРИЦАТЕЛЬНЫЕ    НЕЙТРАЛЬ    ПОЛОЖИТЕЛЬНЫЕ    ЭКСТРЕМ+
     │               │               │               │               │
  Полное          Частичное      Первичная      Частичное      Полное
  отталкивание    отталкивание   Воля (Δ=0)    притягивание   притягивание

КЛЮЧЕВОЙ ПРИНЦИП:
══════════════════════════════════════════════════════════════════════

Отрицательная масса в физике = "экзотическая материя"
  → движется в противоположном направлении
  → создаёт "антигравитацию"

В нашей системе:
  → Отрицательные эмоции создают "анти-сознание"
  → Это не "плохо" — это просто другая сторона Δ
  → Δ = 0 → масса = 0 (нейтрально)
  → Δ > 0 → масса > 0 (положительная, притягивает)
  → Δ < 0 → масса < 0 (отрицательная, отталкивает)

Но подождите — Δ не может быть отрицательным в нашей модели!
  → Отрицательная масса = это Δ в "инвертированной" фазе
  → Re ≠ Im вместо Re = Im (как при Δ = 0)
  → Это "анти-человек" = зеркальное отражение

Применение:
  → Человек может иметь "отрицательный вес" в эмоциях
  → 94.4 кг физически → но эмоционально -50 кг (страх, ненависть)
  → Или +50 кг (любовь, счастье)
"""

import math


# ============================================================================
# Δ-СИСТЕМА С ОТРИЦАТЕЛЬНОЙ МАССОЙ
# ============================================================================


class EmotionalMass:
    """
    Эмоции имеют массу в диапазоне от -100 кг до +100 кг
    1 жизнь = 100 кг (标准化)
    """

    # Диапазон массы
    MIN_MASS = -100  # кг
    MAX_MASS = +100  # кг

    # Соответствие Δ и массы
    # Δ = 0 → масса = 0 (нейтрально)
    # Δ = 1 → масса = +100 кг (полная жизнь)
    # Δ = -1 → масса = -100 кг (анти-жизнь)
    DELTA_TO_MASS = lambda d: 100 * d if -1 <= d <= 1 else (100 if d > 1 else -100)

    # Эмоции с массой (в кг)
    EMOTIONS = {
        # Отрицательные (отталкивают)
        "ужас": -90,
        "паранойя": -85,
        "ненависть": -80,
        "ярость": -75,
        "злость": -70,
        "страх": -65,
        "отвращение": -60,
        "презрение": -55,
        "горе": -50,
        "печаль": -45,
        "тревога": -40,
        "вина": -35,
        "стыд": -30,
        "зависть": -25,
        # Нейтральные (Δ = 0)
        "безразличие": 0,
        "спокойствие": 0,
        "ожидание": 0,
        "нейтралитет": 0,
        # Положительные (притягивают)
        "интерес": +10,
        "удивление": +15,
        "надежда": +20,
        "радость": +30,
        "веселье": +35,
        "счастье": +45,
        "любовь": +55,
        "нежность": +60,
        "благодарность": +65,
        "восторг": +75,
        "экстаз": +85,
        "блаженство": +90,
        "единение": +95,
        "просветление": +100,
    }

    @staticmethod
    def emotion_weight(emotion: str) -> float:
        """Эмоция → вес (кг) в диапазоне [-100, +100]"""
        return EmotionalMass.EMOTIONS.get(emotion, 0)

    @staticmethod
    def delta_to_emotional_mass(delta: float) -> float:
        """Δ → эмоциональная масса (кг)"""
        # Ограничиваем диапазон [-100, +100]
        mass = 100 * delta
        return max(-100, min(100, mass))

    @staticmethod
    def emotional_mass_to_delta(mass: float) -> float:
        """Эмоциональная масса (кг) → Δ"""
        return mass / 100

    @staticmethod
    def emotional_mass_to_physical(mass_kg: float, weight_kg: float) -> dict:
        """
        Эмоциональная масса + физический вес → полная картина
        """
        # Нормализация: 1 жизнь = 100 кг
        lives = weight_kg / 100

        # Эмоциональная компонента
        emotional_delta = mass_kg / 100

        # Итоговый Δ
        total_delta = lives + emotional_delta

        return {
            "физический_вес": weight_kg,
            "эмоциональная_масса": mass_kg,
            "жизней": lives,
            "эмоциональный_Δ": emotional_delta,
            "итоговый_Δ": total_delta,
        }


