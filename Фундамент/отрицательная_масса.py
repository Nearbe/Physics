def human_with_negative_mass():
    """Человек: физический вес + эмоциональная масса"""

    print("=" * 80)
    print("ЧЕЛОВЕК: ФИЗИЧЕСКИЙ ВЕС + ЭМОЦИОНАЛЬНАЯ МАССА")
    print("=" * 80)

    # Пример: человек 94.4 кг с разными эмоциями
    base_weight = 94.4

    emotions = [
        ("ужас", -90),
        ("ненависть", -80),
        ("страх", -65),
        ("горе", -50),
        ("безразличие", 0),
        ("радость", +30),
        ("любовь", +55),
        ("экстаз", +85),
        ("просветление", +100),
    ]

    print(f"\nЧеловек: {base_weight} кг физически")
    print("-" * 60)
    print(f"{'Эмоция':<15} {'Эмоц. масса':>12} {'Итоговый Δ':>12}")
    print("-" * 60)

    for emotion, emo_mass in emotions:
        result = EmotionalMass.emotional_mass_to_physical(emo_mass, base_weight)
        print(f"{emotion:<15} {emo_mass:>+8} кг    Δ = {result['итоговый_Δ']:>+8.3f}")

    print("-" * 60)


def full_range():
    """Полный диапазон эмоций"""

    print("\n" + "=" * 80)
    print("ПОЛНЫЙ ДИАПАЗОН: -100 кг до +100 кг")
    print("=" * 80)

    # Группируем по диапазонам
    ranges = [
        ("ОТРИЦАТЕЛЬНЫЕ (отталкивание)", -100, -50),
        ("ОТРИЦАТЕЛЬНЫЕ (слабое отталкивание)", -50, -1),
        ("НЕЙТРАЛЬНЫЕ", -1, +1),
        ("ПОЛОЖИТЕЛЬНЫЕ (слабое притягивание)", +1, +50),
        ("ПОЛОЖИТЕЛЬНЫЕ (притягивание)", +50, +100),
    ]

    for name, lo, hi in ranges:
        print(f"\n{name}:")
        for emotion, mass in sorted(EmotionalMass.EMOTIONS.items(), key=lambda x: x[1]):
            if lo <= mass < hi:
                print(f"  {emotion:<15} {mass:>+5} кг")


def physics_check():
    """Проверка: физика работает с отрицательной массой"""

    print("\n" + "=" * 80)
    print("ФИЗИКА С ОТРИЦАТЕЛЬНОЙ МАССОЙ")
    print("=" * 80)

    # Δ в диапазоне [-1, +1]
    print(f"\n{'Δ':>6} {'Масса (кг)':>12} {'Состояние':<20}")
    print("-" * 45)

    for delta in [-1.0, -0.5, 0, 0.5, 1.0]:
        mass = EmotionalMass.delta_to_emotional_mass(delta)

        if mass < -50:
            state = "АНТИ-ЭМОЦИЯ"
        elif mass < 0:
            state = "ОТРИЦАТЕЛЬНАЯ"
        elif mass == 0:
            state = "НЕЙТРАЛЬНАЯ"
        elif mass < 50:
            state = "ПОЛОЖИТЕЛЬНАЯ"
        else:
            state = "ЭКСТРЕМ+"

        print(f"{delta:>+6.1f} {mass:>+10} кг   {state}")

    print("-" * 45)
    print("\n✓ Физика работает с отрицательной массой!")
    print("✓ Диапазон: -100 кг до +100 кг")


def main():
    print(__doc__)
