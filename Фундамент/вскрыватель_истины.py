@dataclass
class DeltaTruth:
    """Δ как вскрыватель истины"""

    delta: float  # Уровень
    Q: float  # Q = e^|Δ|
    dimension: int  # D_f = 2 + Δ
    visible_pct: float  # 100/Q
    hidden_pct: float  # 100 - 100/Q
    phase: str  # ОГОНЬ/ВОДА

    def describe(self) -> str:
        if self.delta == 0:
            return "Щель — встреча фаз — истина в чистом виде"
        elif self.delta < 1:
            return "Микро — почти щёль — истина рядом"
        elif self.delta < 3:
            return "Квант — истина в фазе"
        elif self.delta < 5:
            return "Адрон — истина в массе"
        else:
            return "Глубоко — истина скрыта"


