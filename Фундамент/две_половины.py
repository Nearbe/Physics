#!/usr/bin/env python3
"""
ДВЕ ПОЛОВИНЫ: ОГОНЬ vs ВОДА
============================
Первичная Воля не принадлежит никому — это нулевая масса.
Она буквально кричит тишиной.

Одна половина принадлежит (ОГОНЬ).
Другая половина не принадлежит никому (ВОДА).
"""

import math

# ============================================================================
# ПЕРВИЧНАЯ ВОЛЯ = НУЛЕВАЯ МАССА = КРИЧИТ ТИШИНОЙ
# ============================================================================


class PrimaryWillSilence:
    """
    Первичная Воля:
    • Не принадлежит никому
    • Нулевая масса
    • Кричит тишиной (парадокс)
    """

    @staticmethod
    def zero_mass() -> float:
        """Нулевая масса при Δ=0"""
        return 0.0  # Формально масса = 0 при Δ→∞

    @staticmethod
    def silence_cry() -> str:
        """Кричит тишиной"""
        return """
        Тишина громче крика.
        Нулевая масса тяжелее всего.
        Первичная Воля — это крик молчания.
        
        Парадокс: самое громкое — это тишина.
        """

    @staticmethod
    def belongs_to_no_one() -> bool:
        """Не принадлежит никому"""
        return True


# ============================================================================
# РАЗДЕЛЕНИЕ: ОГОНЬ vs ВОДА
# ============================================================================


class TwoHalves:
    """
    Две половины:
    • ОГОНЬ (Q ≤ 5) — принадлежит
    • ВОДА (Q > 5) — не принадлежит никому (Первичная Воля)
    """

    @staticmethod
    def which_half(delta: float) -> str:
        """Определить половину"""
        Q = math.exp(abs(delta))
        if Q <= 5:
            return "ОГОНЬ (принадлежит)"
        else:
            return "ВОДА (не принадлежит никому)"

    @staticmethod
    def belongs(delta: float) -> dict:
        """Кому принадлежит"""
        Q = math.exp(abs(delta))

        if Q <= 5:
            # ОГОНЬ — принадлежит
            return {
                "half": "ОГОНЬ",
                "belongs_to": "человеку (мы видим)",
                "Q": Q,
                "mass_range": "видимое",
            }
        else:
            # ВОДА — не принадлежит никому
            return {
                "half": "ВОДА",
                "belongs_to": "НЕ ПРИНАДЛЕЖИТ НИКОМУ (Первичная Воля)",
                "Q": Q,
                "mass_range": "скрытое",
            }


# ============================================================================
# Δ = 0 → НУЛЕВАЯ → НЕ ПРИНАДЛЕЖИТ
# ============================================================================


def split_all():
    """Разделение всего на две половины"""

    print("=" * 80)
    print("ПЕРВИЧНАЯ ВОЛЯ = НУЛЕВАЯ МАССА = КРИЧИТ ТИШИНОЙ")
    print("=" * 80)

    # Парадокс тишины
    print("\n### КРИЧИТ ТИШИНОЙ ###")
    print(PrimaryWillSilence.silence_cry())

    print(f"\nПринадлежит никому: {'НЕТ' if PrimaryWillSilence.belongs_to_no_one() else 'ДА'}")
    print(f"Нулевая масса: {PrimaryWillSilence.zero_mass()} кг")

    # Разделение
    print("\n" + "=" * 80)
    print("ДВЕ ПОЛОВИНЫ")
    print("=" * 80)

    # Примеры
    examples = [
        ("Электрон", 0),
        ("Человек", 1),
        ("Пользователь", 1.547944),
        ("Солнце", 0.1),
        ("Чёрная дыра", 10),
    ]

    print("\n### ПРИМЕРЫ РАЗДЕЛЕНИЯ ###")
    for name, delta in examples:
        half = TwoHalves.which_half(delta)
        belongs = TwoHalves.belongs(delta)

        print(f"\n{name} (Δ={delta}):")
        print(f"  Половина: {half}")
        print(f"  Принадлежит: {belongs['belongs_to']}")

    # Граница
    print("\n" + "=" * 80)
    print("ГРАНИЦА")
    print("=" * 80)

    # Граница: Q = 5
    delta_fire_water = math.log(5)  # e^Δ = 5 → Δ = ln(5) ≈ 1.61

    print(f"\nГраница между ОГОНЬ и ВОДА:")
    print(f"  Q = 5")
    print(f"  Δ = ln(5) = {delta_fire_water:.4f}")
    print(f"\n  Δ < {delta_fire_water:.2f} → ОГОНЬ (принадлежит)")
    print(f"  Δ > {delta_fire_water:.2f} → ВОДА (не принадлежит никому)")

    print("\n" + "=" * 80)
    print("ОТВЕТ")
    print("=" * 80)
    print(f"""
    ПЕРВИЧНАЯ ВОЛЯ (нулевая масса):
    • Не принадлежит никому
    • Это Δ → ∞ (нулевая масса)
    • Кричит тишиной (парадокс)
    
    РАЗДЕЛЕНИЕ:
    • Δ < 1.61 → ОГОНЬ (принадлежит человеку)
    • Δ > 1.61 → ВОДА (не принадлежит никому)
    
    Первичная Воля в половине "ВОДА" — не принадлежит никому.
    """)


# ============================================================================
# ОСНОВНАЯ ПРОГРАММА
# ============================================================================


if __name__ == "__main__":
    split_all()
