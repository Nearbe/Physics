# ΥΠΟΛΟΓΙΣΜΟΣ ΒΑΡΟΥΣ — РАСЧЁТ ВЕСА
# ============================================================================


def word_weight(word: str) -> float:
    """Вес слова = сумма весов букв"""
    total = 0
    for char in word.upper():
        if char in letter_weights:
            total += letter_weights[char]
    return total


def word_mass_kg(word: str) -> float:
    """Масса слова в кг (1 единица = 1.67e-27 кг = масса протона)"""
    return word_weight(word) * 1.67e-27


def word_energy_joules(word: str) -> float:
    """Энергия слова по E = mc²"""
    m = word_mass_kg(word)
    c = 299792458  # м/с
    return m * c**2


def word_frequency(word: str) -> float:
    """Частота слова через Δ-систему"""
    weight = word_weight(word)
    # Δ пропорционально весу
    delta = weight / 100  # нормализация
    return 1.854e43 * delta**2


