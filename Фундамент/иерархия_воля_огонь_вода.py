#!/usr/bin/env python3
"""
ИЕРАРХИЯ: ПЕРВИЧНАЯ ВОЛЯ → ОГОНЬ + ВОДА
=========================================
Первичная Воля — первична, нулевая масса, кричит тишиной.
ОГОНЬ и ВОДА — вторичны, имеют свою массу.

ОГОНЬ → принадлежит (имеет массу)
ВОДА → не принадлежит (имеет массу)
"""

import math

# ============================================================================
# ПЕРВИЧНАЯ ВОЛЯ (ПЕРВИЧНАЯ)
# ============================================================================


class PrimaryWill:
    """
    Первичная Воля:
    • Первична (была первой)
    • Нулевая масса (Δ → ∞)
    • Кричит тишиной
    • Не принадлежит никому
    """

    @staticmethod
    def is_primary() -> bool:
        return True  # Первичная

    @staticmethod
    def mass() -> float:
        return 0.0  # Нулевая масса

    @staticmethod
    def description() -> str:
        return """
        ПЕРВИЧНАЯ ВОЛЯ:
        • Была первой (до огня и воды)
        • Нулевая масса (не имеет веса)
        • Кричит тишиной (парадокс)
        • Не принадлежит никому
        """


# ============================================================================
# ОГОНЬ + ВОДА (ВТОРИЧНЫЕ, ИМЕЮТ МАССУ)
# ============================================================================


class FireWaterMass:
    """
    ОГОНЬ и ВОДА — вторичны, имеют массу
    """

    @staticmethod
    def fire_mass(delta: float) -> float:
        """Масса ОГНЯ (Q ≤ 5)"""
        Q = math.exp(abs(delta))
        if Q <= 5:
            m_0 = 1212.34
            return m_0 * math.exp(-Q)
        return 0

    @staticmethod
    def water_mass(delta: float) -> float:
        """Масса ВОДЫ (Q > 5)"""
        Q = math.exp(abs(delta))
        if Q > 5:
            m_0 = 1212.34
            return m_0 * math.exp(-Q)
        return 0

    @staticmethod
    def is_belong(delta: float) -> dict:
        """Принадлежность"""
        Q = math.exp(abs(delta))

        if Q <= 5:
            return {
                "phase": "ОГОНЬ",
                "has_mass": True,
                "mass_kg": FireWaterMass.fire_mass(delta),
                "belongs": "человеку (принадлежит)",
            }
        else:
            return {
                "phase": "ВОДА",
                "has_mass": True,
                "mass_kg": FireWaterMass.water_mass(delta),
                "belongs": "НЕ ПРИНАДЛЕЖИТ (Первичная Воля)",
            }


# ============================================================================
# ИЕРАРХИЯ: ВОЛЯ → ОГОНЬ/ВОДА
# ============================================================================


def hierarchy():
    """Иерархия: Воля → Огонь → Вода"""

    print("=" * 80)
    print("ИЕРАРХИЯ")
    print("=" * 80)

    # Первичная Воля
    print("\n### ПЕРВИЧНАЯ ВОЛЯ (ПЕРВИЧНАЯ) ###")
    print(PrimaryWill.description())

    # ОГОНЬ и ВОДА
    print("\n### ОГОНЬ + ВОДА (ВТОРИЧНЫЕ) ###")
    print("ОГОНЬ и ВОДА имеют массу:")

    examples = [
        ("Электрон", 0),
        ("Человек", 1),
        ("Пользователь", 1.55),
        ("Чёрная дыра", 10),
    ]

    for name, delta in examples:
        result = FireWaterMass.is_belong(delta)

        print(f"\n{name} (Δ={delta}):")
        print(f"  Фаза: {result['phase']}")
        print(f"  Масса: {result['mass_kg']:.6f} кг")
        print(f"  Принадлежит: {result['belongs']}")

    # Граница
    print("\n" + "=" * 80)
    print("ГРАНИЦА: Δ = 1.61 (Q = 5)")
    print("=" * 80)

    print("""
    Δ < 1.61 → ОГОНЬ → имеет массу → принадлежит
    Δ > 1.61 → ВОДА → имеет массу → не принадлежит
    
    Первичная Воля (нулевая масса) → ПЕРЕД огнём и водой
    """)

    print("\n" + "=" * 80)
    print("ВЫВОД")
    print("=" * 80)
    print("""
    1. ПЕРВИЧНАЯ ВОЛЯ — первична, нулевая масса, не принадлежит никому
    2. ОГОНЬ — вторичен, имеет массу, принадлежит
    3. ВОДА — вторична, имеет массу, не принадлежит
    
    Порядок:
    ПЕРВИЧНАЯ ВОЛЯ → ОГОНЬ / ВОДА
    
    Масса есть и у ОГНЯ и у ВОДЫ.
    """)


# ============================================================================
# ОСНОВНАЯ ПРОГРАММА
# ============================================================================


if __name__ == "__main__":
    hierarchy()
