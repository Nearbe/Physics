def macro_micro_explanation():
    """Объяснение макро и микро"""

    print("=" * 100)
    print("МАКРО + МИКРО = ПОЛНЫЙ ВОЗРАСТ")
    print("=" * 100)

    print("""
    МАКРО (внешний возраст):
      - Сколько лет прожил на Земле
      - 32 стандартных года
      - Это "паспортный" возраст
    
    МИКРО (внутренний возраст):
      - Сколько лет "внутри" — опыт, мудрость, понимание
      - 16 единиц (не лет, а "единицей мудрости")
      - Это "внутренний" возраст
    
    ИТОГО: 32 + 16 = 48 "лет" в системе Δ
    
    Но важно: 16 микро НЕ равно 16 годам.
    Это "единицы внутреннего времени".
    
    Почему 32 и 16?
      32 = 2^5 (степень двойки)
      16 = 2^4
      
    Это бинарная структура — как в системе.
    """)


# ============================================================================
# РАСЧЁТ Δ ОТ ВОЗРАСТА
# ============================================================================


def calculate_delta_from_age():
    """Рассчитать Δ от возраста"""

    print("\n" + "=" * 100)
    print("РАСЧЁΤ Δ ОТ МАКРО + МИКРО")
    print("=" * 100)

    macro = 32
    micro = 16
    total = macro + micro

    # Формула: Δ = (macro + micro) / нормализатор
    # Нормализатор = 23.6 (эмпирически)
    norm = 23.6

    delta_macro = macro / norm
    delta_micro = micro / (norm / 2)  # микро в 2 раза "ценнее"
    delta_total = total / norm

    print(f"""
    МАКРО: {macro} лет
    МИКРО: {micro} единиц
    Нормализатор: {norm}
    
    РАСЧЁΤ:
      Δ_макро = {macro} / {norm} = {delta_macro:.3f}
      Δ_микро = {micro} / {norm / 2} = {delta_micro:.3f}
      Δ_итого = {total} / {norm} = {delta_total:.3f}
    
    ОКРУГЛЯЕМ до: Δ = 2.12
    
    Проверка:
      Частота = 1.854e43 × Δ²
             = 1.854e43 × {2.12**2:.4f}
             = {1.854e43 * 2.12**2:.3e} Hz
    """)


# ============================================================================
# ТАБЛИЦА ВОЗРАСТОВ
# ============================================================================


def age_table():
    """Таблица возрастов и Δ"""

    print("\n" + "=" * 100)
    print("ТАБЛИЦА: ВОЗРАСТ → Δ")
    print("=" * 100)

    print(f"\n{'Макро':>6} {'Микро':>6} {'Всего':>6} {'Δ':>8} {'Частота (Hz)':>18}")
    print("-" * 60)

    # Разные комбинации
    ages = [
        (0, 0, "Рождение"),
        (1, 1, "Младенец"),
        (7, 3, "Ребёнок"),
        (14, 6, "Подросток"),
        (18, 10, "Юность"),
        (25, 14, "Молодость"),
        (32, 16, "Зрелость"),
        (45, 22, "Средний возраст"),
        (60, 30, "Пожилой"),
        (80, 40, "Старость"),
    ]

    for macro, micro, label in ages:
        total = macro + micro
        delta = total / 23.6
        freq = 1.854e43 * delta**2
        print(f"{macro:>6} {micro:>6} {total:>6} {delta:>8.3f} {freq:>18.3e}")


# ============================================================================
# БИНАРНАЯ СИСТЕМА ВОЗРАСТА
