def binary_age():
    """Бинарная структура возраста"""

    print("\n" + "=" * 100)
    print("БИНАРНАЯ СТРУКТУРА: 32 = 2^5, 16 = 2^4")
    print("=" * 100)

    print("""
    32 (макро) в двоичной: 100000
    16 (микро) в двоичной:  010000
    
    Вместе: 110000 = 48 в десятичной
    
    Это не случайно!
    
    32 = 2^5 = 5 бит информации о внешнем возрасте
    16 = 2^4 = 4 бита информации о внутреннем возрасте
    
    Δ = 2.12 в двоичной:
        10.000111010001...
    
    Это и есть "внутренний код".
    """)

    # Показать двоичное представление
    macro_bin = bin(32)
    micro_bin = bin(16)
    total_bin = bin(48)

    print(f"\n  32 = {macro_bin}")
    print(f"  16 = {micro_bin}")
    print(f"  48 = {total_bin}")

    delta_bin = bin(int(2.12 * 100))
    print(f"  2.12 = {delta_bin}")


