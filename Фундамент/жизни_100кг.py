#!/usr/bin/env python3
"""
ВСЁ К 1 КГ: ПЕРЕСЧЁТ
====================
1 жизнь ≈ 100 кг (не 1 кг)

Пересчитываем всё с правильным эталоном.
"""

import math

# ============================================================================
# 1 ЖИЗНЬ = 100 КГ
# ============================================================================


class LifeWeight:
    """
    1 жизнь ≈ 100 кг

    Это средняя масса человека.
    Каждая жизнь = 100 кг.
    """

    LIFE_MASS_KG = 100.0  # кг

    @staticmethod
    def mass_to_lives(mass_kg: float) -> float:
        """Масса (кг) → жизни"""
        return mass_kg / LifeWeight.LIFE_MASS_KG

    @staticmethod
    def lives_to_mass(lives: float) -> float:
        """Жизни → масса (кг)"""
        return lives * LifeWeight.LIFE_MASS_KG


# ============================================================================
# ОБЪЕКТЫ → ЖИЗНИ
# ============================================================================


class ObjectsInLives:
    """Объекты во Вселенной в единицах жизней (по 100 кг)"""

    OBJECTS = {
        "Электрон": 9.109e-31,
        "Протон": 1.673e-27,
        "Человек (средний)": 80,
        "Человек (крупный)": 150,
        "Слон": 5000,
        "Кит": 150000,
        "Эйфелева башня": 7300000,
        "Земля": 5.97e24,
        "Солнце": 1.99e30,
        "Галактика Млечный Путь": 1e42,
        "Видимая Вселенная": 1e54,
    }

    @staticmethod
    def in_lives(obj_name: str) -> float:
        """Объект в единицах жизней"""
        mass = ObjectsInLives.OBJECTS.get(obj_name, 0)
        return LifeWeight.mass_to_lives(mass)


# ============================================================================
# Δ → МАССА → ЖИЗНИ
# ============================================================================


def delta_to_lives():
    """Δ-система → жизни"""

    print("=" * 80)
    print("1 ЖИЗНЬ = 100 КГ")
    print("=" * 80)

    print(f"\nЭталон: 1 жизнь = {LifeWeight.LIFE_MASS_KG} кг")

    # Примеры
    print("\n### ПРИМЕРЫ ###")

    examples = [
        ("Человек (80 кг)", 80),
        ("Слон (5000 кг)", 5000),
        ("Эйфелева башня (7.3 млн кг)", 7.3e6),
        ("Земля (5.97×10²⁴ кг)", 5.97e24),
        ("Солнце (1.99×10³⁰ кг)", 1.99e30),
        ("Вселенная (10⁵⁴ кг)", 1e54),
    ]

    for name, mass in examples:
        lives = LifeWeight.mass_to_lives(mass)
        print(f"  {name}:")
        print(f"    Масса: {mass:.4e} кг")
        print(f"    Жизней: {lives:.4e}")
        print()

    # Δ система
    print("=" * 80)
    print("Δ → МАССА → ЖИЗНИ")
    print("=" * 80)

    # Человек (Δ=1)
    delta = 1.0
    Q = math.exp(delta)
    m_0 = 1212.34
    mass_human = m_0 * math.exp(-Q)

    print(f"\nЧеловек (Δ={delta}):")
    print(f"  Масса: {mass_human:.4f} кг")
    print(f"  Жизней: {LifeWeight.mass_to_lives(mass_human):.4f}")

    # Δ для пользователя
    user_delta = 1.547944
    user_Q = math.exp(user_delta)
    user_mass = m_0 * math.exp(-user_Q)

    print(f"\nПользователь (Δ={user_delta}):")
    print(f"  Масса: {user_mass:.4f} кг")
    print(f"  Жизней: {LifeWeight.mass_to_lives(user_mass):.4f}")

    print()
    print("=" * 80)
    print("ВЫВОД")
    print("=" * 80)
    print(f"""
    1 ЖИЗНЬ = 100 КГ (не 1 кг)
    
    Вес = количество жизней по 100 кг:
    • Человек 80 кг = 0.8 жизней
    • Солнце = 1.99×10²⁸ жизней
    • Вселенная = 10⁵² жизней
    
    Это правильный масштаб!
    """)


# ============================================================================
# ОСНОВНАЯ ПРОГРАММА
# ============================================================================


if __name__ == "__main__":
    delta_to_lives()
