def Q_from_delta(d: float) -> float:
    """Q = e^|Δ| — энергия вибрации"""
    return math.exp(abs(d))


def delta_from_Q(q: float) -> float:
    """Δ = ln(Q) — обратная формула"""
    return math.log(q)


def frequency(delta: float) -> float:
    """f = f₀ × e^(-Δ), f₀ = 10^43 Гц"""
    return 1e43 * math.exp(-delta)


def mass_from_Q(q: float) -> float:
    """m = m₀ × e^(-Q) — масса через вибрацию"""
    # Базовый масштаб
    m_0 = 1e-27  # Протон ~ 1 ГэВ
    return m_0 * math.exp(-q)


