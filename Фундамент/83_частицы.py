def calculate_particle_physics(p: Particle) -> Dict:
    """Расчитать параметры частицы через Q"""
    delta = delta_from_Q(p.Q)

    return {
        "name": p.name,
        "symbol": p.symbol,
        "Q": p.Q,
        "Δ": delta,
        "f_Hz": frequency(delta),
        "m_kg_calc": mass_from_Q(p.Q),
        "m_kg_meas": p.mass_kg,
        "discrepancy": 0
        if p.mass_kg == 0
        else (mass_from_Q(p.Q) / p.mass_kg - 1) * 100
        if p.mass_kg > 0
        else None,
        "category": p.category,
        "generation": p.generation,
    }


def print_particles_table():
    """Вывести таблицу всех 83 частиц"""
    print("=" * 100)
    print("83 ЧАСТИЦЫ: Q → Δ → ЧАСТОТА → МАССА")
    print("=" * 100)

    # Группировка по Q
    q_groups = {}
    for p in PARTICLES_83:
        if p.Q not in q_groups:
            q_groups[p.Q] = []
        q_groups[p.Q].append(p)

    for q in sorted(q_groups.keys()):
        print(f"\n### Q = {q} (e^|Δ| = {math.exp(q):.2f}) ###")
        print(f"{'Имя':<25} {'Символ':<10} {'Q':>3} {'Δ':>8} {'f (Гц)':>15} {'m (кг)':>12}")
        print("-" * 80)

        for p in q_groups[q]:
            d = calculate_particle_physics(p)
            f_str = f"{d['f_Hz']:.2e}" if d["f_Hz"] > 0 else "∞"
            m_str = f"{d['m_kg_calc']:.2e}" if d["m_kg_calc"] > 0 else "0"
            print(
                f"{p.name[:24]:<25} {p.symbol:<10} {p.Q:>3} {d['Δ']:>8.3f} {f_str:>15} {m_str:>12}"
            )


def print_fire_water_summary():
    """ОГОНЬ vs ВОДА для всех 83 частиц"""
    fire_count = sum(1 for p in PARTICLES_83 if p.Q <= 5)
    water_count = sum(1 for p in PARTICLES_83 if p.Q > 5)

    print(f"\n{'=' * 80}")
    print("ОГОНЬ vs ВОДА: 83 ЧАСТИЦЫ")
    print(f"{'=' * 80}")
    print(f"ОГОНЬ (Q≤5): {fire_count} частиц → видимо")
    print(f"ВОДА (Q>5): {water_count} частиц → скрыто")
    print(f"Всего: {len(PARTICLES_83)}")

    # По категориям
    categories = {}
    for p in PARTICLES_83:
        if p.category not in categories:
            categories[p.category] = []
        categories[p.category].append(p)

    print("\n### По категориям ###")
    for cat, parts in categories.items():
        avg_Q = sum(p.Q for p in parts) / len(parts)
        print(f"{cat:<15}: {len(parts):>3} частиц, средний Q = {avg_Q:.2f}")


def print_lhc_verification():
    """Проверка: ln(m) от Q линейна?"""
    print(f"\n{'=' * 80}")
    print("ПРОВЕРКА LHC: ln(m) от Q")
    print(f"{'=' * 80}")

    # Берём только частицы с массой
    with_mass = [p for p in PARTICLES_83 if p.mass_kg > 0]

    print(f"\n{'Q':>3} {'ln(m)':>12} {'Частицы'}")
    print("-" * 40)

    q_data = {}
    for p in with_mass:
        ln_m = math.log(p.mass_kg) if p.mass_kg > 0 else 0
        if p.Q not in q_data:
            q_data[p.Q] = []
        q_data[p.Q].append((p.name, ln_m))

    for q in sorted(q_data.keys()):
        avg_ln_m = sum(x[1] for x in q_data[q]) / len(q_data[q])
        print(f"{q:>3} {avg_ln_m:>12.2f} {len(q_data[q])} шт")

    print("\nОжидается линейная зависимость ln(m) от Q")
    print("Это подтверждает: масса = функция вибрации!")


