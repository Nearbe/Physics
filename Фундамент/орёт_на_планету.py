#!/usr/bin/env python3
"""
СХЕМА: ОРЁТ НА ПЛАНЕТУ → БОЛЬШОЙ ВЗРЫВ
========================================
Первичная Воля орет на планету.
Мы видим Большой взрыв.

Это как будто:
• Кто-то кричит на планету сверху
• Мы видим только след (Большой взрыв)
• Но источник — крик (Первичная Воля)
"""

import math

# ============================================================================
# СХЕМАТИЧЕСКОЕ ПРЕДСТАВЛЕНИЕ
# ============================================================================


class BigBangShout:
    """
    Схема: Орет на планету → Большой взрыв

    Первичная Воля (крик/вибрация)
           ↓
    "Орёт" на планету (воздействует)
           ↓
    Мы видим: БОЛЬШОЙ ВЗРЫВ (след)
    """

    @staticmethod
    def schematic() -> str:
        return """
╔══════════════════════════════════════════════════════════════════╗
║                                                                  ║
║                 ПЕРВИЧНАЯ ВОЛЯ (крик/вибрация)                    ║
║                        ↓ орёт                                     ║
║                 ═══════════════════════                           ║
║                      ↓ на планету                                 ║
║                 ═══════════════════════                           ║
║                            ↓                                       ║
║               ┌───────────────────────┐                           ║
║               │  БОЛЬШОЙ ВЗРЫВ         │  ← Мы видим               ║
║               │  (след/отражение)      │                           ║
║               └───────────────────────┘                           ║
║                                                                  ║
╚══════════════════════════════════════════════════════════════════╝
        """

    @staticmethod
    def explanation() -> str:
        return """
АНАЛОГИЯ:
• Кто-то кричит в горах → эхо возвращается
• Первичная Воля "орёт" на планету → мы видим Большой взрыв

МЫ ВИДИМ:   Большой взрыв (след)
РЕАЛЬНО:    Крик Первичной Воли (причина)

Это как:
• Тень на стене — мы видим тень, но источник — рука
• Большой взрыв — мы видим взрыв, но источник — крик Воли
        """


# ============================================================================
# ПАРАМЕТРЫ БОЛЬШОГО ВЗРЫВА
# ============================================================================


class BigBangParams:
    """Параметры Большого взрыва"""

    AGE = 13.8e9  # лет
    TEMPERATURE = 1e32  # К (в начале)
    ENERGY = 1e68  # Дж

    @staticmethod
    def from_will(delta: float) -> dict:
        """Большой взрыв из Воли (Δ)"""
        Q = math.exp(delta)

        # Δ→∞ → Q→∞ → Большой взрыв
        return {
            "delta_needed": "∞ (нулевая масса)",
            "Q_needed": "∞",
            "result": "Большой взрыв",
            "our_delta": delta,
            "we_are": "отражение того крика",
        }


# ============================================================================
# ВИЗУАЛИЗАЦИЯ
# ============================================================================


def visualize():
    """Визуализация схемы"""

    print("=" * 80)
    print("СХЕМА: ОРЁТ НА ПЛАНЕТУ → БОЛЬШОЙ ВЗРЫВ")
    print("=" * 80)

    print(BigBangShout.schematic())

    print("\n### ОБЪЯСНЕНИЕ ###")
    print(BigBangShout.explanation())

    print("\n" + "=" * 80)
    print("ПАРАМЕТРЫ")
    print("=" * 80)

    bp = BigBangParams.from_will(1.55)
    print(f"\nДля создания Большого взрыва:")
    print(f"  Δ нужно: {bp['delta_needed']}")
    print(f"  Q нужно: {bp['Q_needed']}")

    print(f"\nНаш Δ = {bp['our_delta']}")
    print(f"Мы = {bp['we_are']}")

    print("\n" + "=" * 80)
    print("ВЫВОД")
    print("=" * 80)
    print("""
    ПЕРВИЧНАЯ ВОЛЯ → ОРЁТ → БОЛЬШОЙ ВЗРЫВ
    
    • Кто-то "орёт" на планету сверху
    • Этот "крик" = Большой взрыв
    • Мы видим только след (взрыв)
    • Причина — крик Воли
    
    Схематически:
    ════════════════→ БОЛЬШОЙ ВЗРЫВ
    (крик/шум Воли)      (мы видим)
    """)


# ============================================================================
# ОСНОВНАЯ ПРОГРАММА
# ============================================================================


if __name__ == "__main__":
    visualize()
