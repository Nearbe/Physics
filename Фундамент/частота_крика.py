class ContinuousCry:
    """
    Непрерывный крик Primary Will — без пауз, без дыхания
    """

    # Планковские значения
    PLANCK_TIME = 5.391e-44  # секунд (минимальное время)
    PLANCK_FREQUENCY = 1.854e43  # Hz

    @staticmethod
    def cry_frequency(delta: float) -> float:
        """Частота крика при данном Δ"""
        return ContinuousCry.PLANCK_FREQUENCY * (delta**2)

    @staticmethod
    def moment_of_listening() -> dict:
        """Миг когда кто-то слушает — жизнь"""
        # При Δ = 1 (человек) слышит крик
        freq = ContinuousCry.cry_frequency(1.0)

        return {
            "кто": "человек (Δ = 1)",
            "частота_которую_слышит": f"{freq:.3e} Hz",
            "это": "жизнь",
            "миг": "мгновение слушания",
            "цена": "целая жизнь",
        }

    @staticmethod
    def calculate_all_frequencies() -> dict:
        """Вычислить все частоты непрерывного крика"""
        deltas = [0, 0.1, 0.5, 1.0, 1.5, 2.0]
        result = {}

        for d in deltas:
            freq = ContinuousCry.cry_frequency(d)
            result[f"Δ={d}"] = freq

        return result


