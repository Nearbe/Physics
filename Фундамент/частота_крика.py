def calculate_decay_frequencies():
    """Вычислить частоты распада (переход от Δ к 0)"""

    print("\n" + "=" * 80)
    print("ЧАСТОТЫ РАСПАДА")
    print("=" * 80)

    # Распад = переход от высокого Δ к низкому
    # f = частота перехода

    transitions = [
        (2.0, 1.5, "звезда"),
        (1.5, 1.0, "человек"),
        (1.0, 0.5, " старость"),
        (0.5, 0.1, "смерть"),
        (0.1, 0.0, "уход"),
    ]

    print("\nПереходы (от какого Δ к какому):")
    print("-" * 60)

    for d_start, d_end, desc in transitions:
        f_start = ContinuousCry.cry_frequency(d_start)
        f_end = ContinuousCry.cry_frequency(d_end)

        # Частота перехода = разность
        f_transition = f_start - f_end

        print(f"  {d_start} → {d_end}: {f_transition:.3e} Hz ({desc})")

    print("\n" + "=" * 80)
    print("КЛЮЧЕВОЙ РЕЗУЛЬТАТ")
    print("=" * 80)
    print("""
    ЧАСТОТА ЖИЗНИ (человек Δ=1): 1.85e43 Hz
    ЧАСТОТА РАСПАДА (смерть):    уменьшается до 0
    
    МЫ ЖИВЁМ В РЕЗОНАНСЕ,
    НЕ В СЛУХЕ!
    
    Резонанс Шумана (7.83 Hz) — мост
    между планковской частотой и нашим телом
    """)


