class Calibrator:
    """Калибровка в поле [-1, +1]"""

    @staticmethod
    def norm(value: float, vmin: float, vmax: float) -> float:
        if vmax == vmin:
            return 0.0
        n = (value - vmin) / (vmax - vmin) * 2 - 1
        return max(-1.0, min(1.0, n))


