class LifeAndDecayFrequencies:
    """
    Вычисляем частоты жизни и распада

    Частота жизни = связана с Δ = 1
    Частота распада = связана с переходом Δ → 0
    """

    # Константы
    PLANCK_FREQUENCY = 1.854e43  # Hz
    SCHUMANN_F1 = 7.83  # Hz

    @classmethod
    def calculate_life_frequency(cls, delta: float) -> float:
        """
        Частота жизни через Δ

        При Δ = 1: частота = частота человека = 1.85e43 Hz
        При Δ → 0: частота → 0 (жизнь угасает)

        Но есть промежуточные уровни — биоритмы!
        """
        return cls.PLANCK_FREQUENCY * (delta**2)

    @classmethod
    def calculate_decay_frequency(cls, delta_start: float, delta_end: float) -> float:
        """
        Частота распада = переход от одного Δ к другому

        dΔ/dt = частота распада
        """
        return cls.PLANCK_FREQUENCY * (delta_start**2 - delta_end**2)

    @classmethod
    def find_optimal_decay_path(cls) -> list:
        """Найти оптимальный путь распада (от жизни к смерти)"""
        path = []
        for delta in [2.0, 1.5, 1.0, 0.5, 0.1, 0.0]:
            freq = cls.calculate_life_frequency(delta)
            path.append({"Δ": delta, "частота": freq})
        return path

    @classmethod
    def connect_to_schumann(cls, delta: float) -> float:
        """
        Связь Δ-частоты с Резонансом Шумана

        Человек (Δ = 1) должен резонировать с f1 = 7.83 Hz
        Это происходит через биоритмы
        """
        # Мозг альфа-ритм = 10 Hz — близко к 7.83
        # Значит биоритмы = мост между Planck-частотой и Schumann

        # Вычисляем коэффициент
        base_freq = cls.calculate_life_frequency(delta)
        ratio = cls.SCHUMANN_F1 / base_freq
        return ratio


