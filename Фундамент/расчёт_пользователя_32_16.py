#!/usr/bin/env python3
"""
РАСЧЁТ ПОЛЬЗОВАТЕЛЯ: 32 МАКРО + 16 МИКРО ЛЕТ
═══════════════════════════════════════════════════════════════════════

Пользователь: 32 макро лет + 16 микро лет
"""

import math


# ============================================================================
# ЛЕТА: МАКРО + МИКРО
# ============================================================================


class UserAge:
    """
    32 макро + 16 микро лет
    """

    MACRO_YEARS = 32
    MICRO_YEARS = 16

    # Общее количество "микро-лет" в макро-годе
    # Если 32 макро = 32×микро, то микро = 1 год = 1 единица
    # Но возможно другая система

    @staticmethod
    def total_micro_years() -> int:
        """Всего микро-лет"""
        return 32 * 16  # если 16 микро в каждом макро
        # Или возможно: 32 + 16 = 48 микро-лет

    @staticmethod
    def normalize_age() -> dict:
        """Нормализация возраста в системе координат"""
        # Возраст 32 макро + 16 микро
        # Примем что 1 макро = 100 микро (или 1 макро = 1 год)

        # Вариант 1: 32 макро = 32 года
        age_years = 32

        # Нормализация в [-1, +1] для возраста
        # Диапазон: 0-150 лет
        normalized = (age_years - 0) / (150 - 0) * 2 - 1

        return {
            "макро_лет": 32,
            "микро_лет": 16,
            "итого_лет": 32,  # если макро = годы
            "нормализовано": normalized,
            "вектор": normalized,
        }

    @staticmethod
    def calculate_personal_frequencies() -> dict:
        """Вычислить личные частоты"""

        # Частота через возраст
        # f = f_base × (age / max_age)

        base_freq = 1.854e43  # Hz (планковская)

        # Вариант 1: возраст как часть жизни
        age_ratio = 32 / 80  # 32 года из 80 ожидаемых

        freq_at_age = base_freq * age_ratio

        # Вариант: микро- годы как дополнительный множитель
        # 16 микро = резонанс
        micro_factor = 16 / 100  # если 100 = полный резонанс

        return {
            "частота_по_возрасту": f"{freq_at_age:.3e} Hz",
            "микро_фактор": micro_factor,
            "итого": f"{freq_at_age * (1 + micro_factor):.3e} Hz",
        }


# ============================================================================
# Δ РАСЧЁТ ДЛЯ ПОЛЬЗОВАТЕЛЯ
# ============================================================================


class UserDelta:
    """
    Расчёт Δ для пользователя с учётом возраста
    """

    # 已知 данные
    LAT = 58.53313
    LON = 31.21913
    WEIGHT_KG = 94.4
    HEIGHT_CM = 175

    MACRO = 32
    MICRO = 16

    @staticmethod
    def calculate() -> dict:
        """Вычислить Δ пользователя"""

        # Базовый Δ из веса
        lives = UserDelta.WEIGHT_KG / 100  # 0.944 жизни
        base_delta = 1.0 + math.log(lives + 1) / 100

        # Уточнение по возрасту (32 макро + 16 микро)
        # Возрастной фактор
        age_factor = UserDelta.MACRO / 80  # 32/80 = 0.4
        micro_factor = UserDelta.MICRO / 100  # 16/100 = 0.16

        # Δ с учётом возраста
        age_delta = base_delta * (1 + age_factor + micro_factor)

        # Итоговый Δ
        final_delta = age_delta + 0.55  # + эмоция (любовь)

        # Частота
        freq = 1.854e43 * (final_delta**2)

        return {
            "вес": f"{UserDelta.WEIGHT_KG} кг",
            "рост": f"{UserDelta.HEIGHT_CM} см",
            "возраст": f"{UserDelta.MACRO} макро + {UserDelta.MICRO} микро",
            "базовый_Δ": base_delta,
            "возрастной_фактор": age_factor,
            "микро_фактор": micro_factor,
            "итоговый_Δ": final_delta,
            "частота": f"{freq:.3e} Hz",
        }


# ============================================================================
# ВЕКТОР ПОЛЬЗОВАТЕЛЯ
# ============================================================================


def user_vector():
    """Вектор пользователя"""

    print("=" * 80)
    print("ПОЛЬЗОВАТЕЛЬ: 32 МАКРО + 16 МИКРО")
    print("=" * 80)

    # Расчёт Δ
    result = UserDelta.calculate()

    print("\nДанные:")
    for k, v in result.items():
        print(f"  {k}: {v}")

    # Нормализация в [-1, +1]
    print("\n" + "=" * 80)
    print("ВЕКТОР В ПРОСТРАНСТВЕ [-1, +1]")
    print("=" * 80)

    # Параметры для нормировки
    params = {
        "вес": 94.4,
        "рост": 175,
        "возраст": 32,
        "широта": 58.53,
        "долгота": 31.22,
    }

    ranges = {
        "вес": (0, 200),
        "рост": (100, 220),
        "возраст": (0, 120),
        "широта": (-90, 90),
        "долгота": (-180, 180),
    }

    print("\nНормализованные значения:")
    for name, value in params.items():
        lo, hi = ranges[name]
        normalized = (value - lo) / (hi - lo) * 2 - 1

        # Визуализация
        bar_len = int((normalized + 1) * 20)
        bar = "─" * bar_len
        if normalized > 0:
            bar += "►"
        elif normalized < 0:
            bar = "◄" + bar
        else:
            bar = "─" * 20 + "┼" + "─" * 20

        print(f"  {name:<10} {value:>8} → {normalized:>+7.3f}  {bar}")


