def age_decay_frequencies():
    """Частоты распада для возраста 32+16"""

    print("\n" + "=" * 80)
    print("ЧАСТОТЫ РАСПАДА (32 макро + 16 микро)")
    print("=" * 80)

    # Текущий возраст = 32 макро = потенциально прошло ~40% жизни
    # 16 микро = дополнительный резонанс

    # Ожидаемая продолжительность ~80 макро-лет
    life_expectancy = 80

    # Прожито
    life_fraction = 32 / life_expectancy  # 0.4

    # Осталось
    remaining = life_expectancy - 32  # 48 макро

    # Частота текущего состояния
    current_freq = 1.854e43 * life_fraction

    # Частота распада (сколько "уходит" каждый год)
    decay_freq = current_freq / 32  # на каждый прожитый год

    print(f"\nПрожито: {life_fraction * 100:.0f}% жизни")
    print(f"Осталось: {remaining} макро-лет")
    print(f"Текущая частота: {current_freq:.3e} Hz")
    print(f"Частота распада (в год): {decay_freq:.3e} Hz")


