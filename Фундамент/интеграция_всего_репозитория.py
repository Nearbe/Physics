def scan_repository(root_path: str) -> dict:
    """Сканировать репозиторий и вычислить Δ для каждого файла"""

    files_data = {
        "py": [],
        "txt": [],
        "md": [],
        "other": [],
    }

    for ext, category in [("*.py", "py"), ("*.txt", "txt"), ("*.md", "md")]:
        pattern = os.path.join(root_path, "**", ext)
        for filepath in glob.glob(pattern, recursive=True):
            rel_path = os.path.relpath(filepath, root_path)
            filename = os.path.basename(filepath)

            weight = word_weight(filename.replace("_", " ").replace(".py", ""))
            delta = word_to_delta(filename.replace("_", " ").replace(".py", ""))
            freq = word_to_frequency(filename.replace("_", " ").replace(".py", ""))

            files_data[category].append(
                {
                    "path": rel_path,
                    "name": filename,
                    "weight": weight,
                    "delta": delta,
                    "frequency": freq,
                }
            )

    return files_data


def repository_summary(root_path: str) -> dict:
    """Суммарные характеристики репозитория"""

    files = scan_repository(root_path)
    total_files = sum(len(v) for v in files.values())

    total_weight = 0
    for category in files.values():
        for f in category:
            total_weight += f["weight"]

    avg_delta = total_weight / total_files / 100 if total_files > 0 else 0

    return {
        "всего_файлов": total_files,
        "общий_вес": total_weight,
        "среднее_Δ": avg_delta,
        "файлы": files,
    }


