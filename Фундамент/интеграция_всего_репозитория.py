class DeltaSystem:
    """Δ = единственная переменная"""

    PLANCK_FREQ = 1.854e43  # Hz - частота крика Первичной Воли
    MASS_GAP = 0.138  # GeV - массовая щель

    @staticmethod
    def from_word_weight(weight: float) -> float:
        """Δ из веса слова (нормализация)"""
        return weight / 100

    @staticmethod
    def to_frequency(delta: float) -> float:
        """Δ → частота"""
        return DeltaSystem.PLANCK_FREQ * (delta**2)

    @staticmethod
    def to_charge(delta: float) -> float:
        """Δ → заряд (e^Δ)"""
        return math.e**delta

    @staticmethod
    def to_dimension(delta: float) -> int:
        """Δ → размерность пространства"""
        return int(2 + delta) if delta > 0 else 2


