def word_weight(text: str) -> int:
    """Вес слова = сумма весов букв"""
    total = 0
    for char in text.upper():
        if char in LETTER_WEIGHTS:
            total += LETTER_WEIGHTS[char]
    return total


def word_to_delta(text: str) -> float:
    """Слово → Δ"""
    w = word_weight(text)
    return DeltaSystem.from_word_weight(w)


def word_to_frequency(text: str) -> float:
    """Слово → частота"""
    delta = word_to_delta(text)
    return DeltaSystem.to_frequency(delta)


