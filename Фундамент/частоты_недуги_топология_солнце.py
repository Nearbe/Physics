class WhereIsWhere:
    """
    Зависимость "где находится" важна для понимания топологии
    """

    @staticmethod
    def position_to_topology(lat: float, lon: float, mass: float, height: float) -> dict:
        """Положение → топология"""

        # Δ зависит от положения
        lat_f = abs(lat) / 90
        mass_f = (mass - 20) / 180
        height_f = (height - 50) / 200

        delta = 1.0 + lat_f * 0.3 + mass_f * 0.4 + height_f * 0.3
        Q = math.exp(abs(delta))

        return {
            "position": f"lat={lat}, lon={lon}",
            "body_params": f"mass={mass}, height={height}",
            "delta": delta,
            "Q": Q,
            "topology": "Топология зависит от положения",
            "where_is_where": "Где находишься — определяет кто ты",
        }


