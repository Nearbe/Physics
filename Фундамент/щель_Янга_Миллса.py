class YangMillsGap:
    """Щель Янга-Миллса через Δ"""

    def __init__(self):
        # Планковская масса
        self.m_pl = 2.176e-8  # кг

        # Щель ~ 1 ГэВ (адронная масса)
        self.gap_energy_GeV = 1.0

        # Δ щели
        self.delta = self._calculate_delta()

    def _calculate_delta(self) -> float:
        """Вычислить Δ щели"""
        # m_0 / m_pl = e^(-Δ)
        # Δ = -ln(m_0/m_pl)

        m_0_kg = self.gap_energy_GeV * 1.602e-10  # 1 ГэВ в кг
        delta = -math.log(m_0_kg / self.m_pl)

        return delta

    def Q(self) -> float:
        return math.exp(self.delta)

    def gap_percent(self) -> float:
        """Щель в процентах"""
        return (1 - 1 / self.Q()) * 100

    def description(self) -> str:
        if self.delta < 1:
            return "микрощель"
        elif self.delta < 3:
            return "квантовая"
        elif self.delta < 5:
            return "адронная"
        else:
            return "глубокая"


