class PtolemyVsFourier:
    """
    Птолемей vs Фурье — противопоставление

    Птолемей: где? (координаты, пространство)
    Фурье: что? (частота, спектр)

    Это как:
    • Геометрия vs Анализ
    • Позиция vs Частота
    • Где находится vs Из чего состоит
    """

    @staticmethod
    def contrast() -> dict:
        return {
            "Птолемей": {
                "вопрос": "Где?",
                "метод": "координаты, эпициклы",
                "область": "пространство",
                "видит": "позицию",
            },
            "Фурье": {
                "вопрос": "Что? (какие частоты)",
                "метод": "разложение в ряд, спектр",
                "область": "частота",
                "видит": "состав",
            },
        }

    @staticmethod
    def in_delta_system() -> dict:
        return {
            "Птолемей": "GPS → Δ = f(lat, lon, mass, height)",
            "Фурье": "Частота → f = f₀×e^(-Q)",
            "вместе": "Где + Что = полная картина",
        }


