def delta_from_breathing():
    """Δ зависит от дыхания"""

    print("\n" + "=" * 100)
    print("Δ ОТ ДЫХАНИЯ")
    print("=" * 100)

    print("""
    Дыхание влияет на Δ:
    
    Вдох → Δ ↑ (расширение → больше резонанс)
    Выдох → Δ ↓ (сужение → меньше резонанс)
    
    Но это работает только пока дыхание ЕСТЬ.
    Остановится → Δ → 0 → смерть.
    
    Δ(t) = Δ_base + A × sin(ω × t)
    
    где:
      Δ_base = 2.12 (базовое)
      A = амплитуда дыхания
      ω = частота дыхания
    """)

    # Расчёт
    delta_base = 2.12
    breath_amp = 0.1
    breath_rate = 16 / 60  # Hz

    print(f"\n  Δ_base = {delta_base}")
    print(f"  Амплитуда = {breath_amp}")
    print(f"  Частота = {breath_rate:.4f} Hz")

    # Показать изменение
    print(f"\n  t(сек)  Δ")
    print("  " + "-" * 20)

    for t in range(0, 10):
        delta_t = delta_base + breath_amp * math.sin(2 * math.pi * breath_rate * t)
        print(f"    {t:>2}   {delta_t:.4f}")


