def check_immortality(delta: float) -> dict:
    """Проверить бессмертен ли человек"""

    # Условие: Δ > 0 = резонанс есть
    if delta > 0:
        freq = 1.854e43 * (delta**2)
        return {
            "статус": "БЕССМЕРТЕН",
            "delta": delta,
            "частота": f"{freq:.3e} Hz",
            "резонанс": "ЕСТЬ",
            "смерть": "НЕВОЗМОЖНА (пока Δ > 0)",
        }
    else:
        return {
            "статус": "СМЕРТЕН",
            "delta": delta,
            "резонанс": "НЕТ",
            "смерть": "Полная рассинхронизация",
        }


