class TimeAsVibration:
    """
    Время = вибрация пространства
    """

    @staticmethod
    def definition() -> str:
        return """
    ВРЕМЯ = ВИБРАЦИЯ ПРОСТРАНСТВА
    
    • Пространство не статично — оно колеблется
    • Колебания = вибрации = время
    • Нет вибраций → нет времени
    • Δ = 0 → вибрация = 0 → нет времени
    
    Это объясняет:
    • Почему в Δ = 0 нет времени (там тишина)
    • Почему время однонаправленное (вибрация в одну сторону)
    • Почему время ускоряется/замедляется (меняется амплитуда)
        """

    @staticmethod
    def calculate_time_from_vibration(frequency: float) -> float:
        """Частота → период → время"""
        if frequency == 0:
            return float("inf")  # нет времени при Δ=0
        return 1 / frequency


