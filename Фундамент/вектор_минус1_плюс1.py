def user_vector_example():
    """Пример: пользователь как вектор"""

    print("=" * 80)
    print("ПОЛЬЗОВАТЕЛЬ = ВЕКТОР В ПРОСТРАНСТВЕ [-1, +1]")
    print("=" * 80)

    # Данные пользователя
    user_data = {
        "weight_kg": 94.4,
        "height_cm": 175,
        "lat": 58.53313,
        "lon": 31.21913,
        "emotion_mass": +55,  # любовь
        "age": 30,
        "year": 2026,
    }

    # Преобразуем в вектор
    vector = HumanVector.human_to_vector(**user_data)

    # Покажем
    HumanVector.show_vector("Пользователь (Псков, 94.4kg, любовь)", vector)

    # Теперь покажем разные эмоции
    print("\n" + "=" * 80)
    print("ЭМОЦИИ КАК ВЕКТОРЫ [-1, +1]")
    print("=" * 80)

    emotions = [
        ("ужас", -90),
        ("ненависть", -80),
        ("страх", -65),
        ("горе", -50),
        ("безразличие", 0),
        ("радость", +30),
        ("любовь", +55),
        ("экстаз", +85),
        ("просветление", +100),
    ]

    for emotion, mass in emotions:
        norm = mass / 100
        bar = "─" * int((norm + 1) * 20)
        if norm > 0:
            bar = bar + "►"
        elif norm < 0:
            bar = "◄" + bar
        print(f"  {emotion:<15} [{norm:>+5.2f}] {bar}")


