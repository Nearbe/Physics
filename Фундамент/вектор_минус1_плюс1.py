#!/usr/bin/env python3
"""
НОРМИРОВКА НА ВЕКТОР [-1, 0, +1]
═══════════════════════════════════════════════════════════════════════

Ключевая идея:
───────────────
Деление по полу (мужское/женское) — это "крик в тишине"
Упрощение реальности до 2 категорий

Бинарность (0/1) работает только при 3 переменных
Но лучше НОРМИРОВАТЬ на вектор [-1, 0, +1]

ПОЧЕМУ ВЕКТОР ЛУЧШЕ:
────────────────────
  Бинарность (0/1):
  ├── Только 2 состояния
  ├── Нет "нуля" (нейтраль)
  └── Нет направления

  Вектор [-1, 0, +1]:
  ├── 3 состояния (-1, 0, +1)
  ├── 0 = нейтраль (Primary Will!)
  ├── Направление (вектор)
  └── Величина (длина вектора)

Это как:
  0 ──────► +1    вместо    0    1
             ▲              │
             │              ▼
            -1

Нормировка всего на [-1, 0, +1]:
════════════════════════════════

• Масса: -1 (антиматерия) ─ 0 (Δ=0) ─ +1 (обычная материя)
• Эмоции: -1 (отталкивание) ─ 0 (нейтраль) ─ +1 (притягивание)
• Время: -1 (прошлое) ─ 0 (сейчас) ─ +1 (будущее)
• Пространство: -1 ─ 0 ─ +1 (направления)
• Сознание: -1 (негативное) ─ 0 (чистое) ─ +1 (позитивное)
• Гендер: -1 (женское) ─ 0 (нейтраль) ─ +1 (мужское)

ВЕКТОРНОЕ ПРЕДСТАВЛЕНИЕ:
────────────────────────

Каждый параметр = вектор в пространстве [-1, 0, +1]^N

Пример: Человек
───────────────
  Масса:      [вес 94.4 кг] → нормализация → +0.944
  Эмоция:     [любовь]      → нормализация → +0.55
  Время:      [2026 год]    → нормализация → +0.5 (относительно 0)
  Направление: [север]     → нормализация → +0.7

  Итоговый вектор: (0.944, 0.55, 0.5, 0.7, ...)

МАТЕМАТИЧЕСКАЯ ЗАПИСЬ:
──────────────────────

Для любого параметра X:
  X_norm = (X - X_min) / (X_max - X_min) × 2 - 1

Где:
  • X_min = минимальное значение
  • X_max = максимальное значение
  • Результат ∈ [-1, +1]

Пример: вес 94.4 кг при диапазоне 0-150 кг
  94.4 / 150 × 2 - 1 = 0.258...
  → нормализация к [-1, +1]
"""

import math


# ============================================================================
# НОРМИРОВЩИК НА ВЕКТОР [-1, 0, +1]
# ============================================================================


class VectorNormalizer:
    """Нормирует любой параметр на вектор [-1, 0, +1]"""

    @staticmethod
    def normalize(value: float, min_val: float, max_val: float) -> float:
        """Нормировать значение на [-1, +1]"""
        if max_val == min_val:
            return 0.0
        normalized = (value - min_val) / (max_val - min_val) * 2 - 1
        return max(-1.0, min(1.0, normalized))

    @staticmethod
    def denormalize(normalized: float, min_val: float, max_val: float) -> float:
        """Обратная нормировка: [-1, +1] → исходное значение"""
        return (normalized + 1) / 2 * (max_val - min_val) + min_val


# ============================================================================
# ПРИМЕНЕНИЕ К ЧЕЛОВЕКУ
# ============================================================================


class HumanVector:
    """
    Человек = вектор в пространстве [-1, +1]^N
    """

    # Параметры человека с диапазонами
    PARAM_RANGES = {
        # Физические
        "вес": (0, 200),  # кг
        "рост": (100, 220),  # см
        "возраст": (0, 120),  # лет
        # Географические
        "широта": (-90, 90),  # градусы
        "долгота": (-180, 180),  # градусы
        "высота": (-1000, 9000),  # метров
        # Эмоциональные (уже в кг → нормируем)
        "эмоция": (-100, 100),  # кг эмоциональной массы
        # Временные (относительно "сейчас" = 0)
        "год": (1900, 2100),
        # Социальные
        "IQ": (50, 150),
        "доход": (0, 1000000),  # USD/год
        # Энергетические
        "Δ": (0, 2),
        "частота": (0, 1e50),
    }

    @staticmethod
    def human_to_vector(
        weight_kg: float,
        height_cm: float,
        lat: float,
        lon: float,
        emotion_mass: float = 0,
        age: int = 30,
        year: int = 2026,
    ) -> dict:
        """
        Человек → вектор в пространстве [-1, +1]
        """
        result = {}

        # Физические параметры
        result["вес"] = VectorNormalizer.normalize(
            weight_kg, HumanVector.PARAM_RANGES["вес"][0], HumanVector.PARAM_RANGES["вес"][1]
        )

        result["рост"] = VectorNormalizer.normalize(
            height_cm, HumanVector.PARAM_RANGES["рост"][0], HumanVector.PARAM_RANGES["рост"][1]
        )

        result["возраст"] = VectorNormalizer.normalize(
            age, HumanVector.PARAM_RANGES["возраст"][0], HumanVector.PARAM_RANGES["возраст"][1]
        )

        # Географические
        result["широта"] = VectorNormalizer.normalize(
            lat, HumanVector.PARAM_RANGES["широта"][0], HumanVector.PARAM_RANGES["широта"][1]
        )

        result["долгота"] = VectorNormalizer.normalize(
            lon, HumanVector.PARAM_RANGES["долгота"][0], HumanVector.PARAM_RANGES["долгота"][1]
        )

        # Эмоциональная масса (уже в диапазоне [-100, +100])
        result["эмоция"] = emotion_mass / 100  # просто делим на 100

        # Временной
        result["год"] = VectorNormalizer.normalize(
            year, HumanVector.PARAM_RANGES["год"][0], HumanVector.PARAM_RANGES["год"][1]
        )

        return result

    @staticmethod
    def show_vector(name: str, data: dict):
        """Показать вектор человека"""
        print(f"\n{'=' * 60}")
        print(f"ВЕКТОР: {name}")
        print("=" * 60)

        for param, value in data.items():
            # Визуализация
            bar_len = int((value + 1) * 20)  # 0-40 символов
            bar = "─" * bar_len
            if value > 0:
                bar = bar + "►"
            elif value < 0:
                bar = "◄" + bar
            else:
                bar = "─" * 20 + "┼" + "─" * 20

            print(f"  {param:<10} {value:>+7.3f}  {bar}")

        print("=" * 60)


