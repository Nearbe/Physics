#!/usr/bin/env python3
"""
ВСЁ К 1 КГ: ЧТО ЕСТЬ ВЕС?
==========================
1 кг = эталон = цена 1 жизни
Каждая физическая константа имеет свой вес.
Вопрос: что есть вес?
"""

import math

# ============================================================================
# ЧТО ТАКОЕ ВЕС?
# ============================================================================


class WhatIsWeight:
    """
    Что есть вес?

    В физике: вес = сила притяжения = mg
    В контексте системы: вес = масса относительно 1 кг

    Вес = количество "жизней" (по 1 кг) в объекте
    """

    @staticmethod
    def physics_definition() -> str:
        """Физическое определение веса"""
        return """
        Вес (W) — сила, с которой тело действует на опору или подвес.
        W = m × g, где:
        • m — масса тела (кг)
        • g — ускорение свободного падения (9.81 м/с²)
        
        На Земле: 1 кг = 9.81 Н (ньютонов)
        """

    @staticmethod
    def system_definition() -> str:
        """Определение в системе координат"""
        return """
        Вес в системе Δ:
        
        Вес = масса относительно 1 кг
        = сколько "жизней" по 1 кг содержится в объекте
        
        Пример:
        • Человек 80 кг = 80 жизней (по 1 кг)
        • Планета 6×10²⁴ кг = 6×10²⁴ жизней
        • Вселенная 10⁵⁴ кг = 10⁵⁴ жизней
        """

    @staticmethod
    def fundamental_definition() -> str:
        """Фундаментальное определение"""
        return """
        ВЕС = мера того, сколько "первичных жизней" (по 1 кг)
        содержится в объекте.
        
        1 кг — это не просто единица массы.
        Это — цена 1-й жизни.
        
        Вес = количество этих "жизней" в объекте.
        """


# ============================================================================
# ВСЕ ФИЗИЧЕСКИЕ КОНСТАНТЫ → КГ
# ============================================================================


class ConstantsToKg:
    """
    Каждая физическая константа имеет вес в кг
    """

    # Физические константы с весом
    CONSTANTS = {
        "c": 299792458,  # скорость света (м/с) — не масса, но имеет "эквивалент массы"
        "h": 6.626e-34,  # постоянная Планка (Дж·с)
        "G": 6.674e-11,  # гравитационная постоянная (м³/кг/с²)
        "m_e": 9.109e-31,  # масса электрона (кг)
        "m_p": 1.673e-27,  # масса протона (кг)
        "m_P": 2.176e-8,  # планковская масса (кг)
        "M_earth": 5.97e24,  # масса Земли (кг)
        "M_sun": 1.989e30,  # масса Солнца (кг)
        "M_universe": 1e54,  # масса Вселенной (кг)
    }

    @staticmethod
    def get_weight(constant_name: str) -> float:
        """Получить вес константы в кг"""
        return ConstantsToKg.CONSTANTS.get(constant_name, 0)

    @staticmethod
    def weight_in_lives(constant_name: str) -> float:
        """Вес в единицах жизней (по 1 кг)"""
        weight_kg = ConstantsToKg.get_weight(constant_name)
        return weight_kg  # 1 жизнь = 1 кг


# ============================================================================
# МАССА ВСЕЛЕННОЙ → КГ
# ============================================================================


class UniverseMass:
    """
    Масса всего во Вселенной в единицах 1 кг
    """

    # Массы объектов
    OBJECTS = {
        "Электрон": 9.109e-31,
        "Протон": 1.673e-27,
        "Атом водорода": 1.674e-27,
        "Молекула воды": 2.992e-26,
        "Человек": 80,
        "Слон": 5000,
        "Кит": 150000,
        "Эйфелева башня": 7300000,
        "Земля": 5.97e24,
        "Солнце": 1.99e30,
        "Чёрная дыра (Солнца)": 2e30,
        "Галактика Млечный Путь": 1e42,
        "Видимая Вселенная": 1e54,
    }

    @staticmethod
    def total_mass_kg() -> float:
        """Общая масса известных объектов (кг)"""
        total = sum(UniverseMass.OBJECTS.values())
        return total

    @staticmethod
    def in_lives() -> float:
        """Общая масса в единицах жизней"""
        return UniverseMass.total_mass_kg()


# ============================================================================
# Δ → МАССА → ВЕС
# ============================================================================


def mass_weight_relationship():
    """Связь массы и веса через Δ"""

    print("=" * 80)
    print("ЧТО ЕСТЬ ВЕС?")
    print("=" * 80)

    # Определения
    print("\n### ФИЗИЧЕСКОЕ ОПРЕДЕЛЕНИЕ ###")
    print(WhatIsWeight.physics_definition())

    print("\n### ОПРЕДЕЛЕНИЕ В СИСТЕМЕ ###")
    print(WhatIsWeight.system_definition())

    print("\n### ФУНДАМЕНТАЛЬНОЕ ОПРЕДЕЛЕНИЕ ###")
    print(WhatIsWeight.fundamental_definition())

    # Константы
    print("\n" + "=" * 80)
    print("ФИЗИЧЕСКИЕ КОНСТАНТЫ → ВЕС В КГ")
    print("=" * 80)

    for name, value in ConstantsToKg.CONSTANTS.items():
        print(f"  {name}: {value:.4e} кг")

    # Вселенная
    print("\n" + "=" * 80)
    print("ОБЪЕКТЫ ВСЕЛЕННОЙ → ВЕС")
    print("=" * 80)

    for name, mass in UniverseMass.OBJECTS.items():
        print(f"  {name}: {mass:.4e} кг")

    # Δ система
    print("\n" + "=" * 80)
    print("Δ → МАССА → ВЕС")
    print("=" * 80)

    # Человек (Δ=1)
    delta = 1.0
    Q = math.exp(delta)
    m_0 = 1212.34
    mass = m_0 * math.exp(-Q)

    print(f"\nЧеловек (Δ={delta}):")
    print(f"  Q = {Q:.4f}")
    print(f"  m = {mass:.4f} кг")
    print(f"  Вес в жизнях = {mass:.4f} (по 1 кг)")

    # Солнце
    print(f"\nСолнце:")
    print(f"  m = {UniverseMass.OBJECTS['Солнце']:.4e} кг")
    print(f"  Вес в жизнях = {UniverseMass.OBJECTS['Солнце']:.4e}")

    # Вселенная
    print(f"\nВселенная:")
    print(f"  m = {UniverseMass.OBJECTS['Видимая Вселенная']:.4e} кг")
    print(f"  Вес в жизнях = {UniverseMass.OBJECTS['Видимая Вселенная']:.4e}")


