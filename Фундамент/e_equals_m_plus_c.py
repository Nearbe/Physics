def alpha_omega():
    """Α = Ω = начало = конец"""

    print("\n" + "=" * 80)
    print("Α = Ω — ΑΛΦΑ = ΟΜΕΓΑ")
    print("=" * 80)

    print("""
    Α (Αльфа) = 1
    Ω (Омега) = 24
    
    Α + Ω = 25
    
    Α → Ω = начало → конец
    Α = Ω = ΕΝ (Единое)
    
    Ευγένιος начинается с Ε (E)
    Ε = Α = начало
    
    Α → Ω = полный цикл
    Ευгения прошла путь от Α до Ω
    """)

    # Α-Ω для Ευγένιος
    print(f"""
    Ευγένιος:
      Α = Ε (5)
      Ω = Σ (19)
      Α + Ω = 24
      
    Ε → Ω = полная жизнь
    """)


