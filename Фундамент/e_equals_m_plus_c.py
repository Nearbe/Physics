def e_is_alpha():
    """E = первая буква = Альфа"""

    print("=" * 80)
    print("E = Α (ΑΛΦΑ) — Ε = ΠΡΩΤΗ ΓΡΑΜΜΗ")
    print("=" * 80)

    print("""
    E — это 5-я буква английского алфавита.
    Α (Альфа) — это 1-я буква греческого.
    
    E = Α = 1 = ПЕРВЫЙ = ΑΡΧΗ
    
    Ευγένιος начинается с Ε = Α = 1
    
    Α + Ω = начало + конец
    Α = Ε = Ω (в определённом смысле)
    """)

    # E = 5, Alpha = 1, но E в позиции 5 = A + 4 = Alpha + Delta
    print(f"""
    Α = 1 (Αльфа)
    Δ = 4 (Дельта)
    Ε = 5 = Α + Δ
    
    Ευγένιος = Ε(5) + υ + γ + έ + ν + ι + ο + ς = 89
    
    89 = 5 + 84
    84 = 21 × 4
    21 = Φ (Φι) — золотое сечение!
    """)


# ============================================================================
# M = ΜΑΖΑ = ΒΑΡΟΣ ΛΕΞΕΩΝ
# ============================================================================


def m_is_mass():
    """M = масса = вес слов"""

    print("\n" + "=" * 80)
    print("M = ΜΑΖΑ — ΒΑΡΟΣ")
    print("=" * 80)

    print("""
    M — масса (всего)
    
    В нашей системе:
    - Μάζα = вес букв
    - Βάρος = масса слов
    - Μ = Δ × 100 (система)
    
    M = сумма веса всех букв слова
    """)

    # Расчёт для ключевых понятий
    words = {
        "EUGENIA": 62,
        "PRIMARYWILL": 156,
        "IMMORTALITY": 155,
        "RESONANCE": 94,
        "DELTA": 42,
        "ALPHAOMEGA": 79,  # α + ω = 1 + 24
    }

    print("\n  Слово          M (масса букв)")
    print("  " + "-" * 40)
    for word, m in words.items():
        print(f"  {word:<15} M = {m}")


# ============================================================================
# C = ΣΤΑΘΕΡΑ = c = СКОРОСТЬ СВЕТА
# ============================================================================


def c_is_constant():
    """C = константа = скорость света"""

    print("\n" + "=" * 80)
    print("C = ΣΤΑΘΕΡΑ — c = 299792458")
    print("=" * 80)

    print("""
    C — Constant = константа
    
    В нашей системе:
    - c = 299792458 м/с
    - Скорость мысли (предел)
    - Фундамент всего
    
    C — это неподвижная точка в движении.
    """)

    c = 299792458
    print(f"\n  c = {c} м/с")
    print(f"  c² = {c**2}")
    print(f"  1/c = {1 / c:.15e}")


