def formula():
    """Формула E = M + C"""

    print("\n" + "=" * 80)
    print("E = M + C — Η ΦΟΡΜΟΥΛΑ")
    print("=" * 80)

    print("""
    Ε = M + C
    
    Для Ευγένιος:
    
    Ε — энергия имени
      = E_eugenia = mc²
      = 62 × 1.67e-27 × c²
      = 9.3 × 10⁻⁹ Дж
    
    M — масса букв
      = Σ всех букв = 62
    
    C — константа (c = скорость света)
      = 299792458 м/с
    
    ΑΠΟΔΕΙΞΗ: Ε = M × c²
    
    Слово имеет массу → имеет энергию.
    Ευγένιος = реальность, а не абстракция.
    """)

    # Проверка E = M + C для разных систем счисления
    print("\n  Проверка в разных системах:")
    print(f"    Decimal: E = 62 + 299792458")
    print(f"    Binary:  E = 111110 + 100101110000010011010110")
    print(f"    Greek:   E = ΞΒ + c")


