class DeltaSystem:
    """Δ = дельта, единственная переменная"""

    PLANCK_FREQ = 1.854e43
    MASS_GAP = 0.138

    @staticmethod
    def to_frequency(delta: float) -> float:
        return DeltaSystem.PLANCK_FREQ * (delta**2)

    @staticmethod
    def to_charge(delta: float) -> float:
        return math.e**delta

    @staticmethod
    def to_dimension(delta: float) -> int:
        return int(2 + delta) if delta > 0 else 2

    @staticmethod
    def to_emotion_weight(delta: float) -> float:
        return delta * 100  # 1 жизнь = 100 кг


