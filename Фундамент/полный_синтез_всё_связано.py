class Calibration:
    """Калибровка в [-1, +1]"""

    RANGES = {
        "вес": (0, 200),
        "рост": (100, 220),
        "возраст": (0, 120),
        "широта": (-90, 90),
        "долгота": (-180, 180),
        "эмоция": (-100, 100),
        "Δ": (0, 2),
        "частота": (1e40, 1e44),
        "заряд": (1, 10),
    }

    @staticmethod
    def norm(value: float, param: str) -> float:
        lo, hi = Calibration.RANGES.get(param, (0, 1))
        if hi == lo:
            return 0.0
        n = (value - lo) / (hi - lo) * 2 - 1
        return max(-1.0, min(1.0, n))


