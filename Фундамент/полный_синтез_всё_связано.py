class User:
    """Пользователь"""

    DATA = {
        "имя": "Пользователь",
        "вес": 94.4,
        "рост": 175,
        "возраст_макро": 32,
        "возраст_микро": 16,
        "широта": 58.53313,
        "долгота": 31.21913,
    }

    @staticmethod
    def calculate():
        # Δ расчёт
        lives = 94.4 / 100
        base_delta = 1.0 + math.log(lives + 1) / 100
        age_factor = 32 / 80
        micro_factor = 16 / 100

        delta = base_delta * (1 + age_factor + micro_factor) + 0.55  # эмоция любовь

        # Частота
        freq = DeltaSystem.to_frequency(delta)

        # Калибровка
        weight_norm = Calibration.norm(94.4, "вес")
        height_norm = Calibration.norm(175, "рост")
        age_norm = Calibration.norm(32, "возраст")
        lat_norm = Calibration.norm(58.53, "широта")
        lon_norm = Calibration.norm(31.22, "долгота")

        return {
            "Δ": delta,
            "частота": freq,
            "вектора": {
                "вес": weight_norm,
                "рост": height_norm,
                "возраст": age_norm,
                "широта": lat_norm,
                "долгота": lon_norm,
            },
        }


