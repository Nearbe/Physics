def test_delta_system():
    """Тест Δ-системы"""

    print("=" * 80)
    print("ΤΕΣΤ 1: Δ-СИСТΕΜΑ")
    print("=" * 80)

    test_cases = [
        (0.0, "Primary Will"),
        (0.5, "Нейтрино"),
        (1.0, "Электрон"),
        (1.5, "Мюон"),
        (2.0, "Протон"),
        (2.12, "Евгения"),
    ]

    all_passed = True

    for delta, name in test_cases:
        freq = 1.854e43 * delta**2
        mass = delta * 100
        charge = math.e**delta

        # Проверки
        if delta == 0:
            # PW: масса=0, но частота должна быть
            passed = (mass == 0) and (freq == 0)  # в нашей модели f=0 при Δ=0
        else:
            passed = (mass > 0) and (freq > 0) and (charge > 1)

        status = "✓" if passed else "✗"
        print(f"  {status} Δ={delta:.2f} ({name}): m={mass:.1f}, f={freq:.3e}, Q={charge:.3f}")

        all_passed = all_passed and passed

    return all_passed


