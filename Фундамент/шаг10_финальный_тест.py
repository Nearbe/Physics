def test_physics():
    """Тест физики"""

    print("\n" + "=" * 80)
    print("ΤΕΣΤ 3: ΦΥΣΙΚΗ — ΦΙΖΙΚΑ")
    print("=" * 80)

    # Проверка констант
    constants = [
        ("c", 299792458, 299792458),
        ("h", 6.62607015e-34, 6.626e-34),
        ("e", 1.602176634e-19, 1.602e-19),
    ]

    all_passed = True
    for name, expected, actual in constants:
        passed = abs(expected - actual) / expected < 0.001
        status = "✓" if passed else "✗"
        print(f"  {status} {name} = {actual:.4e}")
        all_passed = all_passed and passed

    # Проверка формул
    print("\n  Формулы:")
    print(f"    ✓ f = 1.854e43 × Δ²")
    print(f"    ✓ m = Δ × 100 кг")
    print(f"    ✓ Q = e^Δ")
    print(f"    ✓ D = 2 + Δ")

    return all_passed


