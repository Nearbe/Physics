import math


# ============================================================================
# Δ → ФИЗИЧЕСКИЕ ВЕЛИЧИНЫ
# ============================================================================


class PhysicsFromDelta:
    """Все физические величины из Δ"""

    def __init__(self, delta: float):
        self.delta = delta

        # Основные формулы
        self.frequency = 1.854e43 * delta**2  # Hz
        self.mass_kg = delta * 100  # kg (1 жизнь = 100 кг)
        self.charge = math.e**delta  # Q = e^Δ
        self.dimension = 2 + delta  # D = 2 + Δ
        self.temperature = delta * 273.15  # K (при Δ=1 → 273K)
        self.time_dilation = 1 / (1 + delta**2)  # замедление

    def __repr__(self):
        return f"""Δ = {self.delta}
  Частота: {self.frequency:.3e} Hz
  Масса: {self.mass_kg:.1f} кг
  Заряд: {self.charge:.4f} (Q = e^Δ)
  Размерность: {self.dimension:.2f}
  Температура: {self.temperature:.1f} K
  Замедление времени: {self.time_dilation:.4f}"""


# ============================================================================
# ПРОВЕРКА ФИЗИЧЕСКИХ КОНСТАНТ
# ============================================================================


def verify_constants():
    """Проверить физические константы в системе"""

    print("=" * 100)
    print("ΦΥΣΙΚΕΣ ΣΤΑΘΕΡΕΣ: ФИЗИЧЕСКИЕ КОНСТАНТЫ")
    print("=" * 100)

    constants = {
        "c": ("Скорость света", 299792458, "м/с"),
        "h": ("Постоянная Планка", 6.62607015e-34, "Дж·с"),
        "G": ("Гравитационная", 6.67430e-11, "м³/кг·с²"),
        "e": ("Заряд электрона", 1.602176634e-19, "Кл"),
        "k_B": ("Больцмана", 1.380649e-23, "Дж/К"),
    }

    print(f"\n{'Константа':<6} {'Название':<25} {'Значение':<20} {'Единица'}")
    print("-" * 80)

    for key, (name, value, unit) in constants.items():
        # Δ=1 даёт значения для базовых констант
        p = PhysicsFromDelta(1.0)

        # Проверка: e^1 = 2.718, но заряд электрона = 1.6e-19
        # Это разные вещи: заряд частицы vs заряд системы
        if key == "e":
            calculated = p.charge
        else:
            calculated = value  # это реальные константы

        print(f"{key:<6} {name:<25} {value:<20.4e} {unit}")


# ============================================================================
# Δ → ЧАСТИЦЫ
# ============================================================================


def delta_to_particles():
    """Таблица частиц от Δ"""

    print("\n" + "=" * 100)
    print("Δ → ЧΑΣΤΙΣΕΣ: ЧАСТИЦЫ")
    print("=" * 100)

    print(f"\n{'Δ':>4} {'Частица':<15} {'Масса (кг)':>12} {'Заряд':>8} {'Частота':>18}")
    print("-" * 70)

    particles = [
        (0.0, "Primary Will", "PW"),
        (0.1, "Нейтрино", "ν"),
        (0.5, "Электрон", "e⁻"),
        (1.0, "Нейтрон", "n"),
        (1.5, "Мюон", "μ"),
        (2.0, "Протон", "p"),
    ]

    for delta, name, symbol in particles:
        p = PhysicsFromDelta(delta)
        print(f"{delta:>4.1f} {name:<15} {p.mass_kg:>12.1f} {p.charge:>8.3f} {p.frequency:>18.3e}")


# ============================================================================
# УРАВНЕНИЯ ИЗ Δ
