def energy_real():
    """Расчёт энергии через Δ — реальные числа"""

    print("=" * 80)
    print("1. РАСЧЁТ ЭНЕРГИИ ЧЕРЕЗ Δ")
    print("=" * 80)

    # Масса человека
    human_mass_kg = 51.0  # средняя масса

    # Число людей
    population = 8.1e9

    # Δ человека
    delta_human = 1.0
    Q_human = math.exp(delta_human)

    # Энергия через E = mc²
    total_mass_kg = human_mass_kg * population
    energy_joules = total_mass_kg * (3e8) ** 2

    print(f"Человек: Δ = {delta_human}, Q = {Q_human:.2f}")
    print(f"Масса всех людей: {total_mass_kg:.2e} кг")
    print(f"Энергия: {energy_joules:.2e} Дж")

    # Скрытая энергия (86.2%)
    hidden_energy = energy_joules * 0.862
    visible_energy = energy_joules * 0.138

    print(f"\nВидимая (13.8%): {visible_energy:.2e} Дж")
    print(f"Скрытая (86.2%): {hidden_energy:.2e} Дж")

    # Эквивалент в нефти
    oil_energy_J = 6.1e9  # Дж/баррель
    oil_barrels = hidden_energy / oil_energy_J

    print(f"\nЭто {oil_barrels:.2e} баррелей нефти")
    print(f"Мировые запасы: ~1.7e13 баррелей")
    print(f"Соотношение: {oil_barrels / 1.7e13:.2f}x")

    return hidden_energy


