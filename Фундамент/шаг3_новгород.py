class NovgorodSystem:
    """Новгород в системе координат"""

    # Координаты Новгорода
    LAT = 58.52  # широта
    LON = 31.27  # долгота

    # Высота над уровнем моря
    ELEVATION = 25  # метров

    # Население (для резонанса)
    POPULATION = 220000  # approx

    def __init__(self):
        self.delta = None
        self.frequency = None
        self.resonance_power = None

    def calculate_from_geo(self):
        """Рассчитать Δ по географии"""

        # Широта влияет на резонанс
        # Север = ближе к Primary Will (полюс = 0 Δ)

        # Нормированная широта: полюс = 0, экватор = 1
        lat_norm = abs(self.LAT) / 90  # 0 = полюс, 1 = экватор

        # Долгота: восток = положительно, запад = отрицательно
        lon_norm = self.LON / 180  # -1 = запад, +1 = восток

        # Δ от широты: севернее = выше Δ (больше резонанс)
        delta_from_lat = 1.0 + (1 - lat_norm) * 1.12

        self.delta = delta_from_lat
        self.frequency = 1.854e43 * self.delta**2

        # Резонансная сила = население × частота
        self.resonance_power = self.POPULATION * self.frequency

        return self.delta, self.frequency, self.resonance_power


