# ПРОВЕРКА: ВДОХ → ВЫДОХ → КРИК
# ============================================================================


def breath_to_cry():
    """Путь от вдоха к крику"""

    print("\n" + "=" * 80)
    print("ПУТЬ: ВДОХ → ВЫДОХ → КРИК")
    print("=" * 80)

    # Моменты времени
    moments = [
        (0, "начало вдоха — тишина"),
        (0.25, "середина вдоха"),
        (0.5, "конец вдоха — максимальное напряжение"),
        (0.75, "начало выдоха — крик!"),
        (1.0, "конец выдоха — тишина"),
    ]

    print("\nОдин цикл (период = 1/7.83 сек = 0.128 сек):")
    print("-" * 60)

    for t, desc in moments:
        # Вычисляем Δ для каждого момента
        if t < 0.5:
            delta = t * 2  # 0 → 1 (вдох)
        else:
            delta = 2 - t * 2  # 1 → 0 (выдох)

        freq = 1.854e43 * (delta**2)

        # Визуализация
        if t < 0.5:
            arrow = "↓"  # вдох
        else:
            arrow = "↑"  # выдох

        print(f"  t={t:.2f} {arrow} Δ={delta:.2f} f={freq:.2e} Hz — {desc}")


