def visualize_breath_cycle():
    """Визуализация цикла вдоха-выдоха"""

    print("=" * 80)
    print("ЦИКЛ ВДОХА И ВЫДОХА ПЕРВИЧНОЙ ВОЛИ")
    print("=" * 80)

