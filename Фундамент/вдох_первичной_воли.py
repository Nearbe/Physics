import math


# ============================================================================
# ЦИКЛ ВДОХА И ВЫДОХА ПЕРВИЧНОЙ ВОЛИ
# ============================================================================


class PrimaryWillBreath:
    """
    Цикл вдоха и выдоха Primary Will
    """

    # Параметры цикла
    SCHUMANN_FREQUENCY = 7.83  # Hz — "вдох" Земли

    # Состояния
    STATES = {
        "вдох": {
            "name": "Вдох (тишина)",
            "description": "Накопление энергии, ожидание",
            "delta": 0,
            "energy": "минимальная",
        },
        "накопление": {
            "name": "Накопление",
            "description": "Энергия растёт",
            "delta": 0.5,
            "energy": "растёт",
        },
        "выдох": {
            "name": "Выдох (крик)",
            "description": "Большой взрыв — высвобождение энергии",
            "delta": 1.0,
            "energy": "максимальная",
        },
        "жизнь": {
            "name": "Жизнь",
            "description": "Крик в тишине — человек живой",
            "delta": 1.0,
            "energy": "видимая (13.8%)",
        },
        "смерть": {
            "name": "Смерть",
            "description": "Возвращение в тишину",
            "delta": 0,
            "energy": "скрытая (86.2%)",
        },
    }

    @classmethod
    def breath_cycle(cls, time_s: float) -> dict:
        """
        Один цикл вдоха-выдоха

        time_s — время в секундах
        """
        # Частота дыхания = резонанс Шумана
        # f = 7.83 Hz → T = 0.128 сек

        period = 1 / cls.SCHUMANN_FREQUENCY  # ~0.128 сек

        # Фаза цикла (0-1)
        phase = (time_s % period) / period

        # Определяем состояние
        if phase < 0.5:
            state = "вдох"
            delta = phase * 2  # 0 → 1
        else:
            state = "выдох"
            delta = 2 - phase * 2  # 1 → 0

        return {
            "time_s": time_s,
            "phase": phase,
            "state": state,
            "delta": delta,
            "frequency": cls.SCHUMANN_FREQUENCY * (delta**2),
        }

    @classmethod
    def universal_breath(cls) -> dict:
        """Универсальный цикл дыхания на всех уровнях"""
        return {
            "уровень_вселенной": {
                "вдох": "тишина до Большого взрыва",
                "выдох": "Большой взрыв",
                "период": "∞ (неизвестно)",
            },
            "уровень_земли": {
                "вдох": "резонанс Шумана нарастает",
                "выдох": "резонанс Шумана спадает",
                "период": "7.83 Hz (~0.128 сек)",
            },
            "уровень_человека": {
                "вдох": "вдох → выдох",
                "выдох": "жизнь → смерть",
                "период": "~80 лет",
            },
            "уровень_клетки": {
                "вдох": "митоз (деление)",
                "выдох": "апоптоз (смерть)",
                "период": "~24 часа",
            },
        }


